{{ config(
    materialized='incremental',
    unique_key='shipment_id'
) }}

with source_shipments as (
    select 
        shipment_id,
        order_id,
        shipped_date,
        carrier,
        tracking_number
    from {{ source('dwh_raw', 'shipments') }}
    {% if is_incremental() %}
        where shipped_date::date > (select coalesce(max(shipped_date), '1900-01-01') from {{ this }})
    {% endif %}
),

source_orders as (
    select 
        order_id,
        order_date
    from {{ source('dwh_raw', 'orders') }}
),

shipments_with_orders as (
    select 
        shipments.shipment_id,
        shipments.order_id,
        shipments.shipped_date::date as shipped_date,
        upper(shipments.carrier) as carrier,
        shipments.tracking_number,
        orders.order_date
    from source_shipments as shipments
    inner join source_orders as orders
        on shipments.order_id = orders.order_id
),

final as (
    select 
        shipment_id,
        order_id,
        shipped_date,
        carrier,
        tracking_number,
        greatest(0, datediff('day', order_date::date, shipped_date)) as days_to_ship
    from shipments_with_orders
)

select * from final