{{ config(
    materialized = 'table',
    alias = 'dim_address'
) }}

WITH source_data AS (
    SELECT
        addresses.address_id,
        addresses.customer_id,
        addresses.address_line1,
        addresses.city,
        addresses.country,
        addresses.inserted_at
    FROM {{ source('dwh_raw', 'addresses') }} AS addresses
),

customer_lookup AS (
    SELECT
        customer_id
    FROM {{ ref('dim_customer') }}
),

validated_addresses AS (
    SELECT
        source_data.address_id,
        source_data.customer_id,
        source_data.address_line1,
        source_data.city,
        source_data.country,
        source_data.inserted_at
    FROM source_data
    INNER JOIN customer_lookup
        ON source_data.customer_id = customer_lookup.customer_id
),

transformed AS (
    SELECT
        address_id,
        customer_id,
        TRIM(address_line1) AS address_line1, -- Trim whitespace
        UPPER(city) AS city, -- Uppercase city -- auto corrected using semantic file
        country,
        inserted_at AS last_updated -- Direct mapping -- auto corrected using semantic file
    FROM validated_addresses
)

SELECT * FROM transformed