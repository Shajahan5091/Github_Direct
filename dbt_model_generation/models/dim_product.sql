WITH source_data AS (
    SELECT
        products.product_id,
        products.product_name,
        products.price,
        products.inserted_at
    FROM {{ source('dwh_raw', 'products') }} AS products
),

transformed AS (
    SELECT
        products.product_id,
        INITCAP(TRIM(products.product_name)) AS product_name, -- auto corrected using semantic file
        products.price,
        CASE 
            WHEN products.price < 50 THEN 'Low'
            WHEN products.price >= 50 AND products.price < 200 THEN 'Mid'
            WHEN products.price >= 200 AND products.price < 500 THEN 'High'
            WHEN products.price >= 500 THEN 'Premium'
        END AS price_band,
        products.inserted_at AS last_updated -- auto corrected using semantic file
    FROM source_data AS products
)

