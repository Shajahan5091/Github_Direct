{{ config(
    materialized = 'incremental'
) }}

WITH source_order_items AS (
    SELECT
        order_item_id,
        order_id,
        product_id,
        quantity,
        unit_price,
        inserted_at
    FROM {{ source('dwh_raw', 'order_items') }}
    {% if var('models')['backfill_flag'] == 'true' %}
        WHERE inserted_at BETWEEN '{{ var("models")["backfill_start_date"] }}' AND '{{ var("models")["backfill_end_date"] }}'
    {% else %}
        {% if is_incremental() %}
            WHERE inserted_at > (SELECT MAX(inserted_at) FROM {{ this }})
        {% endif %}
    {% endif %}
),

source_orders AS (
    SELECT
        order_id
    FROM {{ source('dwh_raw', 'orders') }}
),

order_items_with_validation AS (
    SELECT
        order_items.order_item_id,
        order_items.order_id,
        CASE 
            WHEN orders.order_id IS NULL THEN NULL
            ELSE order_items.order_id
        END AS validated_order_id,
        CASE 
            WHEN order_items.product_id IS NULL THEN '0'  -- auto corrected using semantic file
            ELSE order_items.product_id
        END AS validated_product_id,
        ROUND(order_items.quantity) AS quantity,  -- Ensure integer semantics; round if fractional
        COALESCE(order_items.unit_price, 0) AS unit_price,  -- If unit_price null treat as 0
        order_items.inserted_at
    FROM source_order_items AS order_items
    LEFT JOIN source_orders AS orders
        ON order_items.order_id = orders.order_id
    WHERE orders.order_id IS NOT NULL  -- Skip item if order_id missing in fact_order
),

calculated_amounts AS (
    SELECT
        order_item_id,
        validated_order_id AS order_id,
        validated_product_id AS product_id,
        quantity,
        unit_price,
        (quantity * unit_price) AS extended_amount,
        0.00 AS discount_amount,  -- Set 0 if no discount context
        inserted_at
    FROM order_items_with_validation
),

final_calculations AS (
    SELECT
        order_item_id,
        order_id,
        product_id,
        quantity,
        unit_price,
        extended_amount,
        discount_amount,
        GREATEST(extended_amount - discount_amount, 0) AS net_amount,  -- Ensure non-negative; floor at 0
        inserted_at AS last_updated
    FROM calculated_amounts
)

SELECT * FROM final_calculations