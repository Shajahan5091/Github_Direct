{{ config(materialized='incremental') }}

with source_order_items as (
    select 
        order_item_id,
        order_id,
        product_id,
        quantity,
        unit_price
    from {{ source('dwh_raw', 'order_items') }}
),

source_orders as (
    select 
        order_id
    from {{ source('dwh_raw', 'orders') }}
),

-- Validate order_id exists in orders table
validated_order_items as (
    select 
        oi.order_item_id,
        oi.order_id,
        oi.product_id,
        oi.quantity,
        oi.unit_price
    from source_order_items oi
    inner join source_orders ord
        on oi.order_id = ord.order_id
),

-- Check if product exists in dim_product, set to 0 if missing -- auto corrected using semantic file
product_validated_items as (
    select 
        voi.order_item_id,
        voi.order_id,
        coalesce(dp.product_id, '0') as product_id,
        voi.quantity,
        voi.unit_price
    from validated_order_items voi
    left join {{ ref('dim_product') }} dp
        on voi.product_id = dp.product_id
),

-- Calculate extended amount and other derived fields
calculated_order_items as (
    select 
        pvi.order_item_id,
        pvi.order_id,
        pvi.product_id,
        round(pvi.quantity) as quantity, -- Ensure integer semantics
        pvi.unit_price,
        round(pvi.quantity) * coalesce(pvi.unit_price, 0) as extended_amount,
        0.00 as discount_amount, -- Set 0 if no discount context
        current_timestamp() as last_updated
    from product_validated_items pvi
),

-- Calculate net amount ensuring non-negative values
final_order_items as (
    select 
        coi.order_item_id,
        coi.order_id,
        coi.product_id,
        coi.quantity,
        coi.unit_price,
        coi.extended_amount,
        coi.discount_amount,
        greatest(coi.extended_amount - coi.discount_amount, 0) as net_amount, -- Floor at 0
        coi.last_updated
    from calculated_order_items coi
)

select 
    order_item_id,
    order_id,
    product_id,
    quantity,
    unit_price,
    extended_amount,
    discount_amount,
    net_amount,
    last_updated
from final_order_items
{% if var('models')['backfill_flag'] == 'true' %}
where last_updated between '{{ var("models")["backfill_start_date"] }}' and '{{ var("models")["backfill_end_date"] }}'
{% endif %}
{% if is_incremental() %}
where last_updated > (select max(last_updated) from {{ this }})
{% endif %}