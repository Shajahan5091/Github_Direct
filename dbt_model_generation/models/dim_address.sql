{{ config(materialized='table') }}

with source_addresses as (
    select
        address_id,
        customer_id,
        address_line1,
        city,
        country,
        inserted_at
    from {{ source('dwh_raw', 'addresses') }}
),

validated_addresses as (
    select
        src_addr.address_id,
        src_addr.customer_id,
        src_addr.address_line1,
        src_addr.city,
        src_addr.country,
        src_addr.inserted_at
    from source_addresses as src_addr
    inner join {{ ref('dim_customer') }} as dim_cust
        on src_addr.customer_id = dim_cust.customer_id
),

final_addresses as (
    select
        validated_addr.address_id,
        validated_addr.customer_id,
        trim(validated_addr.address_line1) as address_line1,
        upper(validated_addr.city) as city,
        validated_addr.country,
        validated_addr.inserted_at::date as inserted_at
    from validated_addresses as validated_addr
)

select * from final_addresses