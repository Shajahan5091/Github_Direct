{{ config(materialized='table') }}

with source_categories as (
    select 
        category_id,
        category_name,
        parent_category_id,
        is_active
    from {{ source('dwh_raw', 'categories') }}
),

parent_categories as (
    select 
        parent_cat.category_id as parent_category_id,
        trim(initcap(parent_cat.category_name)) as parent_category_name
    from {{ source('dwh_raw', 'categories') }} parent_cat
),

transformed_categories as (
    select 
        src.category_id,
        trim(initcap(src.category_name)) as category_name,
        src.parent_category_id,
        case 
            when src.parent_category_id is not null and parent_cat.parent_category_name is not null
            then parent_cat.parent_category_name || ' > ' || trim(initcap(src.category_name))
            else trim(initcap(src.category_name))
        end as category_path,
        coalesce(src.is_active, true) as is_active
    from source_categories src
    left join parent_categories parent_cat
        on src.parent_category_id = parent_cat.parent_category_id
)

select 
    category_id,
    category_name,
    parent_category_id,
    category_path,
    is_active
from transformed_categories