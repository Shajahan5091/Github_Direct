{#
------------------------------------------------------------------------
MODEL NAME    : fact_order
TARGET TABLE  : bsl_ma.dwh_ma.fact_order
SOURCE TABLES : bsl_raw.dwh_raw.orders
DESCRIPTION   : Fact table for order data with customer lookup and 
                status normalization. Includes order details with 
                validated customer references and standardized status values.
PREREQUISITES : dim_customer table must exist for customer lookup
PARAMETER     : Incremental load based on last_updated timestamp
AUTHOR        : AI Generated by Shajahan - 2025-11-18
------------------------------------------------------------------------
#}

{{ config(
    materialized='incremental',
    unique_key='order_id'
) }}

with source_orders as (
    select
        order_id,
        customer_id,
        order_date,
        status,
        total_amount
    from {{ source('dwh_raw', 'orders') }}
    {% if is_incremental() %}
        where order_date > (select max(last_updated) from {{ this }})
    {% endif %}
),

validated_orders as (
    select
        source_orders.order_id,
        source_orders.customer_id,
        source_orders.order_date::date as order_date,
        case 
            when upper(source_orders.status) in ('PENDING', 'CANCELLED', 'SHIPPED', 'DELIVERED') 
            then upper(source_orders.status)
            else 'PENDING'
        end as order_status,
        case 
            when source_orders.total_amount < 0 
            then null
            else source_orders.total_amount
        end as total_amount
    from source_orders
),

final_orders as (
    select
        validated_orders.order_id,
        validated_orders.customer_id,
        validated_orders.order_date,
        validated_orders.order_status,
        validated_orders.total_amount,
        current_timestamp() as last_updated
    from validated_orders
    inner join {{ ref('dim_customer') }} as dim_customer
        on validated_orders.customer_id = dim_customer.customer_id
)

select * from final_orders