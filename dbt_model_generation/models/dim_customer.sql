MODEL NAME    : dim_customer
TARGET TABLE  : bsl_ma.dwh_ma.dim_customer
SOURCE TABLES : bsl_raw.dwh_raw.customers, bsl_raw.dwh_raw.orders
DESCRIPTION   : Customer dimension table with derived customer tier based on
                spending patterns in the last 365 days. Includes data quality
                transformations for names and email validation.
PREREQUISITES : Source tables customers and orders must be available
PARAMETER     : None
AUTHOR        : AI Generated by Shajahan - 2025-11-17
------------------------------------------------------------------------
#}

{{ config(materialized='view') }}

with customers_base as (
    select
        customer_id,
        trim(upper(first_name)) as first_name,
        trim(upper(last_name)) as last_name,
        case 
            when email rlike '^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,}$' 
            then lower(trim(email))
            else null
        end as email,
        coalesce(date(inserted_at), current_date()) as customer_since_date
    from {{ source('dwh_raw', 'customers') }}
),

customer_spending as (
    select
        orders.customer_id,
        sum(orders.total_amount) as total_spending_365_days
    from {{ source('dwh_raw', 'orders') }} as orders
    where orders.order_date >= dateadd('day', -365, current_date())
    group by orders.customer_id
),

final as (
    select
        customers_base.customer_id,
        customers_base.first_name,
        customers_base.last_name,
        customers_base.email,
        customers_base.customer_since_date,
        case
            when coalesce(customer_spending.total_spending_365_days, 0) >= 1000 then 'Gold'
            when coalesce(customer_spending.total_spending_365_days, 0) >= 500 then 'Silver'
            else 'Bronze'
        end as customer_tier
    from customers_base
    left join customer_spending
        on customers_base.customer_id = customer_spending.customer_id
)

select * from final