{{ config(materialized='view') }}

with customer_base as (
    select
        customers.customer_id,
        -- Auto corrected using semantic file: first_nm -> first_name
        trim(upper(customers.first_name)) as first_name,
        -- Auto corrected using semantic file: last_nm -> last_name  
        trim(upper(customers.last_name)) as last_name,
        case 
            when regexp_like(lower(trim(customers.email)), '^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,}$')
            then lower(trim(customers.email))
            else null
        end as email,
        case 
            when customers.inserted_at is null 
            then current_date()
            else customers.inserted_at::date
        end as customer_since_date,
        customers.inserted_at as last_updated
    from {{ source('dwh_raw', 'customers') }} as customers
),

customer_orders as (
    select
        orders.customer_id,
        sum(orders.total_amount) as total_order_amount_365_days
    from {{ source('dwh_raw', 'orders') }} as orders
    where orders.order_date >= dateadd('day', -365, current_date())
    group by orders.customer_id
),

final as (
    select
        customer_base.customer_id,
        customer_base.first_name,
        customer_base.last_name,
        customer_base.email,
        customer_base.customer_since_date,
        case 
            when coalesce(customer_orders.total_order_amount_365_days, 0) >= 1000 then 'Gold'
            when coalesce(customer_orders.total_order_amount_365_days, 0) >= 500 then 'Silver'
            else 'Bronze'
        end as customer_tier,
        customer_base.last_updated
    from customer_base
    left join customer_orders 
        on customer_base.customer_id = customer_orders.customer_id
)

select * from final