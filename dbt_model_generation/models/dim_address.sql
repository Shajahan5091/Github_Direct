{{ config(materialized='table') }}

with source_addresses as (
    select 
        addresses.address_id,
        addresses.customer_id,
        addresses.address_line1,
        addresses.city,
        addresses.country,
        addresses.inserted_at
    from {{ source('dwh_raw', 'addresses') }} as addresses
),

dim_customer_lookup as (
    select 
        customer_id
    from {{ ref('dim_customer') }}
),

transformed_addresses as (
    select 
        source_addresses.address_id,
        source_addresses.customer_id,
        trim(source_addresses.address_line1) as address_line1,
        upper(source_addresses.city) as city,
        source_addresses.country,
        source_addresses.inserted_at as last_updated
    from source_addresses
    inner join dim_customer_lookup 
        on source_addresses.customer_id = dim_customer_lookup.customer_id
)

select 
    address_id,
    customer_id,
    address_line1,
    city,
    country,
    last_updated
from transformed_addresses