with source_categories as (
    select
        category_id,
        category_name,
        parent_category_id,
        is_active,
        inserted_at
    from {{ source('dwh_raw', 'categories') }}
),

parent_categories as (
    select
        category_id as parent_id,
        category_name as parent_name
    from {{ source('dwh_raw', 'categories') }}
),

transformed_categories as (
    select
        src.category_id,
        trim(initcap(src.category_name)) as category_name,
        src.parent_category_id,
        case 
            when src.parent_category_id is not null and parent.parent_name is not null
            then trim(initcap(parent.parent_name)) || ' > ' || trim(initcap(src.category_name))
            else trim(initcap(src.category_name))
        end as category_path,
        coalesce(src.is_active, true) as is_active -- auto corrected using semantic file: default true if null
    from source_categories src
    left join parent_categories parent
        on src.parent_category_id = parent.parent_id
)

select
    category_id,
    category_name,
    parent_category_id,
    category_path,
    is_active
from transformed_categories