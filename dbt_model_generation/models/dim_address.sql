{{ config(materialized='table') }}

with source_addresses as (
    select 
        address_id,
        customer_id,
        address_line1,
        city,
        country,
        inserted_at
    from {{ source('dwh_raw', 'addresses') }}
),

source_customers as (
    select 
        customer_id
    from {{ source('dwh_raw', 'customers') }}
),

validated_addresses as (
    select 
        addresses.address_id,
        addresses.customer_id,
        addresses.address_line1,
        addresses.city,
        addresses.country,
        addresses.inserted_at
    from source_addresses as addresses
    inner join source_customers as customers
        on addresses.customer_id = customers.customer_id
),

final as (
    select 
        address_id,
        customer_id,
        trim(address_line1) as address_line1,
        upper(city) as city,
        country,
        date(inserted_at) as inserted_at, -- auto corrected using semantic file
        current_timestamp() as last_updated
    from validated_addresses
)

select * from final