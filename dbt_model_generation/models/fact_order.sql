{{ config(
    materialized = 'incremental',
    alias = 'fact_order'
) }}

WITH source_data AS (
    SELECT
        orders.order_id,
        orders.customer_id,
        orders.order_date,
        orders.status,
        orders.total_amount,
        orders.inserted_at
    FROM {{ source('dwh_raw', 'orders') }} AS orders
    {% if var('models')['backfill_flag'] == 'true' %}
        WHERE orders.inserted_at BETWEEN '{{ var("models")["backfill_start_date"] }}' AND '{{ var("models")["backfill_end_date"] }}'
    {% else %}
        {% if is_incremental() %}
            WHERE orders.inserted_at > (SELECT MAX(inserted_at) FROM {{ this }})
        {% endif %}
    {% endif %}
),

customer_lookup AS (
    SELECT
        customer_id
    FROM {{ ref('dim_customer') }}
),

validated_orders AS (
    SELECT
        source_data.order_id,
        source_data.customer_id,
        source_data.order_date,
        source_data.status,
        source_data.total_amount,
        source_data.inserted_at
    FROM source_data
    INNER JOIN customer_lookup
        ON source_data.customer_id = customer_lookup.customer_id
),

transformed AS (
    SELECT
        validated_orders.order_id,
        validated_orders.customer_id,
        DATE(validated_orders.order_date) AS order_date, -- auto corrected using semantic file
        CASE 
            WHEN UPPER(validated_orders.status) IN ('PENDING', 'CANCELLED', 'SHIPPED', 'DELIVERED') 
            THEN UPPER(validated_orders.status)
            ELSE 'PENDING'
        END AS order_status,
        CASE 
            WHEN validated_orders.total_amount < 0 
            THEN NULL
            ELSE validated_orders.total_amount
        END AS total_amount,
        validated_orders.inserted_at AS last_updated, -- auto corrected using semantic file
        CURRENT_TIMESTAMP() AS last_updated_timestamp
    FROM validated_orders
)

SELECT * FROM transformed