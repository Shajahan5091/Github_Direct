{{ config(materialized='table') }}

with source_categories as (
    select 
        category_id,
        category_name,
        parent_category_id,
        is_active
    from {{ source('dwh_raw', 'categories') }}
),

parent_categories as (
    select 
        category_id as parent_id,
        category_name as parent_name
    from {{ source('dwh_raw', 'categories') }}
),

transformed_categories as (
    select 
        source_categories.category_id,
        trim(initcap(source_categories.category_name)) as category_name,
        source_categories.parent_category_id,
        case 
            when source_categories.parent_category_id is not null 
            then concat(
                trim(initcap(parent_categories.parent_name)), 
                ' > ', 
                trim(initcap(source_categories.category_name))
            )
            else trim(initcap(source_categories.category_name))
        end as category_path,
        coalesce(source_categories.is_active, true) as is_active
    from source_categories
    left join parent_categories 
        on source_categories.parent_category_id = parent_categories.parent_id
)

select 
    category_id,
    category_name,
    parent_category_id,
    category_path,
    is_active
from transformed_categories