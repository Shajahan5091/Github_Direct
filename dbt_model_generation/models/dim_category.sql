with source_categories as (
    select 
        categories.category_id,
        categories.category_name,
        categories.parent_category_id,
        categories.is_active,
        categories.inserted_at
    from {{ source('dwh_raw', 'categories') }} as categories
),

parent_categories as (
    select 
        parent_cat.category_id as parent_category_id,
        parent_cat.category_name as parent_category_name
    from {{ source('dwh_raw', 'categories') }} as parent_cat
),

transformed_categories as (
    select 
        source_cat.category_id,
        trim(initcap(source_cat.category_name)) as category_name,
        source_cat.parent_category_id,
        case 
            when source_cat.parent_category_id is not null and parent_cat.parent_category_name is not null
            then trim(initcap(parent_cat.parent_category_name)) || ' > ' || trim(initcap(source_cat.category_name))
            else trim(initcap(source_cat.category_name))
        end as category_path,
        coalesce(source_cat.is_active, true) as is_active, -- auto corrected using semantic file: if null default true
        current_timestamp() as last_updated
    from source_categories as source_cat
    left join parent_categories as parent_cat
        on source_cat.parent_category_id = parent_cat.parent_category_id
)

select 
    category_id,
    category_name,
    parent_category_id,
    category_path,
    is_active,
    last_updated
from transformed_categories