{{ config(materialized='view') }}

with customers_base as (
    select
        customer_id,
        trim(upper(first_name)) as first_name,
        trim(upper(last_name)) as last_name,
        case 
            when email ~ '^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}$' 
            then lower(trim(email))
            else null
        end as email,
        coalesce(inserted_at::date, current_date()) as customer_since_date
    from {{ source('dwh_raw', 'customers') }}
),

orders_summary as (
    select
        orders.customer_id,
        coalesce(sum(orders.total_amount), 0) as total_spending_365_days
    from {{ source('dwh_raw', 'orders') }} as orders
    where orders.order_date >= current_date() - 365
    group by orders.customer_id
),

final as (
    select
        customers_base.customer_id,
        customers_base.first_name,
        customers_base.last_name,
        customers_base.email,
        customers_base.customer_since_date,
        case
            when coalesce(orders_summary.total_spending_365_days, 0) >= 1000 then 'Gold'
            when coalesce(orders_summary.total_spending_365_days, 0) >= 500 then 'Silver'
            else 'Bronze'
        end as customer_tier
    from customers_base
    left join orders_summary
        on customers_base.customer_id = orders_summary.customer_id
)

select * from final