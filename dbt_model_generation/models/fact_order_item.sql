{{
  config(
    materialized='incremental',
    unique_key='order_item_id'
  )
}}

with source_order_items as (
    select 
        order_item_id,
        order_id,
        product_id,
        quantity,
        unit_price
    from {{ source('dwh_raw', 'order_items') }}
    {% if is_incremental() %}
        where exists (select 1 where true) and {{ is_incremental() }}
    {% endif %}
),

source_orders as (
    select 
        order_id
    from {{ source('dwh_raw', 'orders') }}
),

fact_order_lookup as (
    select 
        order_id
    from {{ ref('fact_order') }}
),

dim_product_lookup as (
    select 
        product_id
    from {{ ref('dim_product') }}
),

transformed_order_items as (
    select 
        source_items.order_item_id,
        source_items.order_id,
        coalesce(dim_prod.product_id, '0') as product_id,
        round(source_items.quantity) as quantity,
        source_items.unit_price,
        round(source_items.quantity) * coalesce(source_items.unit_price, 0) as extended_amount,
        0 as discount_amount
    from source_order_items as source_items
    inner join fact_order_lookup as fact_ord
        on source_items.order_id = fact_ord.order_id
    left join dim_product_lookup as dim_prod
        on source_items.product_id = dim_prod.product_id
),

final_calculations as (
    select 
        order_item_id,
        order_id,
        product_id,
        quantity,
        unit_price,
        extended_amount,
        discount_amount,
        greatest(0, extended_amount - discount_amount) as net_amount
    from transformed_order_items
)

select 
    order_item_id,
    order_id,
    product_id,
    quantity,
    unit_price,
    extended_amount,
    discount_amount,
    net_amount
from final_calculations