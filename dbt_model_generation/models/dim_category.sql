with source_categories as (
    select
        category_id,
        category_name,
        parent_category_id,
        is_active
    from {{ source('dwh_raw', 'categories') }}
),

parent_categories as (
    select
        category_id as parent_id,
        category_name as parent_name
    from {{ source('dwh_raw', 'categories') }}
),

transformed_categories as (
    select
        source_cat.category_id,
        trim(initcap(source_cat.category_name)) as category_name,
        source_cat.parent_category_id,
        case 
            when source_cat.parent_category_id is not null 
            then trim(initcap(parent_cat.parent_name)) || ' > ' || trim(initcap(source_cat.category_name))
            else trim(initcap(source_cat.category_name))
        end as category_path,
        coalesce(source_cat.is_active, true) as is_active
    from source_categories as source_cat
    left join parent_categories as parent_cat
        on source_cat.parent_category_id = parent_cat.parent_id
)

select
    category_id,
    category_name,
    parent_category_id,
    category_path,
    is_active
from transformed_categories