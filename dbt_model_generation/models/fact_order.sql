{{ config(
    materialized='incremental',
    unique_key='order_id'
) }}

with source_orders as (
    select * from {{ source('dwh_raw', 'orders') }}
),

transformed_orders as (
    select
        orders.order_id,
        orders.customer_id,
        orders.order_date::date as order_date, -- Convert timestamp to date
        case 
            when upper(orders.status) in ('PENDING', 'CANCELLED', 'SHIPPED', 'DELIVERED') 
            then upper(orders.status)
            else 'PENDING' -- If value outside list set to 'PENDING'
        end as order_status,
        case 
            when orders.total_amount < 0 then null -- If negative set to null and log
            else orders.total_amount
        end as total_amount,
        orders.inserted_at as last_updated, -- Auto corrected using semantic file: mapped from inserted_at
        current_timestamp() as last_updated_timestamp -- Added for incremental materialization
    from source_orders as orders
    
    {% if var('models')['backfill_flag'] == 'true' %}
        where orders.inserted_at between '{{ var("models")["backfill_start_date"] }}' and '{{ var("models")["backfill_end_date"] }}'
    {% else %}
        {% if is_incremental() %}
            where orders.inserted_at > (select max(last_updated) from {{ this }})
        {% endif %}
    {% endif %}
)

select * from transformed_orders