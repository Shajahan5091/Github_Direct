TARGET TABLE  : bsl_ma.dwh_ma.dim_product
SOURCE TABLES : bsl_raw.dwh_raw.products
DESCRIPTION   : Dimension table for products with transformed price bands
                and standardized product names
PREREQUISITES : Source table bsl_raw.dwh_raw.products must exist
