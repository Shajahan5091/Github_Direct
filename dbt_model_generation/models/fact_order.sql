MODEL NAME    : fact_order
TARGET TABLE  : BSL_MA.DWH_MA.FACT_ORDER
SOURCE TABLES : BSL_RAW.DWH_RAW.ORDERS
DESCRIPTION   : This model creates a fact table for orders by transforming
                raw order data with proper data type conversions, status
                normalization, and data quality validations.
PREREQUISITES : Source table BSL_RAW.DWH_RAW.ORDERS must exist
PARAMETER     : None
AUTHOUR       : AI Generated by Shajahan - 2025-11-19
------------------------------------------------------------------------
#}

{{ config(
    materialized='incremental',
    unique_key='order_id'
) }}

with source_orders as (
    select 
        order_id,
        customer_id,
        order_date,
        status,
        total_amount
    from {{ source('dwh_raw', 'orders') }}
    {% if is_incremental() %}
        where order_date > (select max(order_date) from {{ this }})
    {% endif %}
),

transformed_orders as (
    select
        orders.order_id,
        orders.customer_id,
        orders.order_date::date as order_date,
        case 
            when upper(orders.status) in ('PENDING', 'CANCELLED', 'SHIPPED', 'DELIVERED') 
            then upper(orders.status)
            else 'PENDING'
        end as order_status,
        case 
            when orders.total_amount < 0 then null
            else orders.total_amount
        end as total_amount,
        current_timestamp() as last_updated
    from source_orders as orders
    where orders.customer_id is not null
)

select 
    order_id,
    customer_id,
    order_date,
    order_status,
    total_amount,
    last_updated
from transformed_orders