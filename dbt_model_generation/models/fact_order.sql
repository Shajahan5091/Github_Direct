MODEL NAME    : fact_order
TARGET TABLE  : BSL_MA.DWH_MA.FACT_ORDER
SOURCE TABLES : BSL_RAW.DWH_RAW.ORDERS
DESCRIPTION   : Fact table for orders containing order details with 
                customer lookups and data transformations
PREREQUISITES : dim_customer table must exist for customer lookups
PARAMETER     : Incremental model based on last_updated timestamp
AUTHOUR       : AI Generated by Shajahan - 2025-11-19
------------------------------------------------------------------------
#}

{{ config(materialized='incremental') }}

with source_orders as (
    select 
        order_id,
        customer_id,
        order_date,
        status,
        total_amount
    from {{ source('dwh_raw', 'orders') }}
    {% if is_incremental() %}
        where last_updated > (select max(last_updated) from {{ this }})
    {% endif %}
),

customer_lookup as (
    select 
        customer_id
    from {{ ref('dim_customer') }}
),

transformed_orders as (
    select 
        src_orders.order_id,
        src_orders.customer_id,
        src_orders.order_date::date as order_date,
        case 
            when upper(src_orders.status) in ('PENDING', 'CANCELLED', 'SHIPPED', 'DELIVERED') 
            then upper(src_orders.status)
            else 'PENDING'
        end as order_status,
        case 
            when src_orders.total_amount < 0 then null
            else src_orders.total_amount
        end as total_amount,
        current_timestamp() as last_updated
    from source_orders src_orders
    inner join customer_lookup cust_lookup
        on src_orders.customer_id = cust_lookup.customer_id
)

select 
    order_id,
    customer_id,
    order_date,
    order_status,
    total_amount,
    last_updated
from transformed_orders