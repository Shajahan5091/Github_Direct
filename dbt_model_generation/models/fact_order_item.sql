{{ config(materialized='incremental') }}

with source_order_items as (
    select 
        order_item_id,
        order_id,
        product_id,
        quantity,
        unit_price
    from {{ source('dwh_raw', 'order_items') }}
    {% if is_incremental() %}
        where last_updated > (select max(last_updated) from {{ this }})
    {% endif %}
),

source_orders as (
    select 
        order_id
    from {{ source('dwh_raw', 'orders') }}
),

-- Validate order_id exists in orders table
validated_order_items as (
    select 
        oi.order_item_id,
        oi.order_id,
        oi.product_id,
        oi.quantity,
        oi.unit_price
    from source_order_items oi
    inner join source_orders o on oi.order_id = o.order_id
),

-- Handle product_id lookup and set to '0' if missing
product_validated_items as (
    select 
        voi.order_item_id,
        voi.order_id,
        case 
            when dp.product_id is null then '0'  -- auto corrected using semantic file
            else voi.product_id 
        end as product_id,
        round(voi.quantity) as quantity,  -- Ensure integer semantics
        coalesce(voi.unit_price, 0) as unit_price  -- Treat null unit_price as 0
    from validated_order_items voi
    left join {{ ref('dim_product') }} dp on voi.product_id = dp.product_id
),

-- Calculate extended amount, discount, and net amount
final_calculations as (
    select 
        pvi.order_item_id,
        pvi.order_id,
        pvi.product_id,
        pvi.quantity,
        pvi.unit_price,
        (pvi.quantity * pvi.unit_price) as extended_amount,
        coalesce(fo.discount_amount, 0) as discount_amount,  -- Set 0 if no discount context
        current_timestamp() as last_updated
    from product_validated_items pvi
    left join {{ ref('fact_order') }} fo on pvi.order_id = fo.order_id
)

select 
    fc.order_item_id,
    fc.order_id,
    fc.product_id,
    fc.quantity,
    fc.unit_price::number(10,2) as unit_price,
    fc.extended_amount::number(12,2) as extended_amount,
    fc.discount_amount::number(12,2) as discount_amount,
    greatest(0, (fc.extended_amount - fc.discount_amount))::number(12,2) as net_amount,  -- Ensure non-negative; floor at 0
    fc.last_updated
from final_calculations fc