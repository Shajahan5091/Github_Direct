TARGET TABLE  : BSL_MA.DWH_MA.DIM_PRODUCT
SOURCE TABLES : BSL_RAW.DWH_RAW.PRODUCTS
DESCRIPTION   : Dimension table for products containing product details
                with price banding and data quality transformations
PREREQUISITES : Source table PRODUCTS must exist in BSL_RAW.DWH_RAW
PARAMETER     : None
AUTHOUR       : AI Generated by Shajahan - 2025-11-20
------------------------------------------------------------------------
