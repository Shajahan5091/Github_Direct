{{ config(materialized='table') }}

with source_addresses as (
    select 
        addresses.ADDRESS_ID,
        addresses.CUSTOMER_ID,
        addresses.ADDRESS_LINE1,
        addresses.CITY,
        addresses.COUNTRY,
        addresses.INSERTED_AT
    from {{ source('dwh_raw', 'addresses') }} as addresses
),

dim_customer_lookup as (
    select 
        customer.CUSTOMER_ID
    from {{ ref('dim_customer') }} as customer
),

transformed_addresses as (
    select 
        source_addr.ADDRESS_ID as address_id,
        source_addr.CUSTOMER_ID as customer_id,
        trim(source_addr.ADDRESS_LINE1) as address_line1,
        upper(source_addr.CITY) as city,
        source_addr.COUNTRY as country,
        date(source_addr.INSERTED_AT) as inserted_at
    from source_addresses as source_addr
    inner join dim_customer_lookup as customer_lookup
        on source_addr.CUSTOMER_ID = customer_lookup.CUSTOMER_ID
)

select * from transformed_addresses