{{ config(materialized='incremental') }}

with source_orders as (
    select 
        order_id,
        customer_id,
        order_date,
        status,
        total_amount
    from {{ source('dwh_raw', 'orders') }}
),

source_customers as (
    select 
        customer_id
    from {{ source('dwh_raw', 'customers') }}
),

-- Validate customers exist in dim_customer
validated_orders as (
    select 
        orders.order_id,
        orders.customer_id,
        orders.order_date,
        orders.status,
        orders.total_amount
    from source_orders as orders
    inner join source_customers as customers
        on orders.customer_id = customers.customer_id
),

transformed_orders as (
    select 
        order_id,
        customer_id,
        order_date::date as order_date, -- Convert timestamp to date
        case 
            when upper(status) in ('PENDING', 'CANCELLED', 'SHIPPED', 'DELIVERED') 
            then upper(status)
            else 'PENDING' -- If value outside list set to 'PENDING'
        end as order_status,
        case 
            when total_amount < 0 then null -- If negative set to null and log
            else total_amount::number(10,2)
        end as total_amount,
        current_timestamp() as last_updated -- Auto corrected using semantic file
    from validated_orders
)

select 
    order_id,
    customer_id,
    order_date,
    order_status,
    total_amount,
    last_updated
from transformed_orders

{% if var('models')['backfill_flag'] == 'true' %}
where last_updated between '{{ var("models")["backfill_start_date"] }}' and '{{ var("models")["backfill_end_date"] }}'
{% endif %}

{% if is_incremental() %}
where last_updated > (select max(last_updated) from {{ this }})
{% endif %}