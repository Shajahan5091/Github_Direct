with source_categories as (
    select * from {{ source('dwh_raw', 'categories') }}
),

parent_categories as (
    select 
        categories.category_id,
        categories.category_name,
        categories.parent_category_id,
        categories.is_active,
        categories.inserted_at,
        parent_cat.category_name as parent_category_name
    from source_categories as categories
    left join source_categories as parent_cat
        on categories.parent_category_id = parent_cat.category_id
),

final as (
    select
        parent_categories.category_id,
        trim(initcap(parent_categories.category_name)) as category_name,
        parent_categories.parent_category_id,
        case 
            when parent_categories.parent_category_name is not null 
            then trim(initcap(parent_categories.parent_category_name)) || ' > ' || trim(initcap(parent_categories.category_name))
            else trim(initcap(parent_categories.category_name))
        end as category_path,
        coalesce(parent_categories.is_active, true) as is_active,
        parent_categories.inserted_at as last_updated
    from parent_categories
)

select * from final