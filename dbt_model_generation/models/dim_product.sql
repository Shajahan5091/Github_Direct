WITH source_data AS (
    SELECT
        product_id,
        product_name,
        price,
        inserted_at
    FROM {{ source('dwh_raw', 'products') }}
),

transformed AS (
    SELECT
        products.product_id,
        INITCAP(TRIM(products.product_name)) AS product_name, -- Title-case product name, auto corrected using semantic file
        CAST(products.price AS NUMBER(10,2)) AS price, -- If price = 0 flag for review in notes
        CASE 
            WHEN products.price < 50 THEN 'Low'
            WHEN products.price >= 50 AND products.price < 200 THEN 'Mid'
            WHEN products.price >= 200 AND products.price < 500 THEN 'High'
            WHEN products.price >= 500 THEN 'Premium'
        END AS price_band,
        products.inserted_at AS last_updated -- auto corrected using semantic file
    FROM source_data AS products
)

