{{ config(materialized='table') }}

with source_addresses as (
    select
        address_id,
        customer_id,
        address_line1,
        city,
        country,
        inserted_at
    from {{ source('dwh_raw', 'addresses') }}
),

validated_customers as (
    select
        customer_id
    from {{ ref('dim_customer') }}
),

transformed_addresses as (
    select
        source_addresses.address_id,
        source_addresses.customer_id,
        trim(source_addresses.address_line1) as address_line1,
        upper(source_addresses.city) as city,
        source_addresses.country,
        date(source_addresses.inserted_at) as inserted_at
    from source_addresses
    inner join validated_customers
        on source_addresses.customer_id = validated_customers.customer_id
)

select
    address_id,
    customer_id,
    address_line1,
    city,
    country,
    inserted_at
from transformed_addresses