{{ config(materialized='table') }}

with source_suppliers as (
    select
        supplier_id,
        supplier_name,
        contact_email,
        country,
        active_flag
    from {{ source('dwh_raw', 'suppliers') }}
),

transformed_suppliers as (
    select
        suppliers.supplier_id as supplier_id,
        
        -- Trim and title-case supplier name
        initcap(trim(suppliers.supplier_name)) as supplier_name,
        
        -- Lowercase email and validate pattern, set null if invalid
        case 
            when suppliers.contact_email is not null 
                 and regexp_like(lower(trim(suppliers.contact_email)), '^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,}$')
            then lower(trim(suppliers.contact_email))
            else null
        end as contact_email,
        
        -- Uppercase country
        upper(trim(suppliers.country)) as country,
        
        -- Direct mapping with null default to false
        coalesce(suppliers.active_flag, false) as is_active
        
    from source_suppliers as suppliers
)

select
    transformed_suppliers.supplier_id,
    transformed_suppliers.supplier_name,
    transformed_suppliers.contact_email,
    transformed_suppliers.country,
    transformed_suppliers.is_active
from transformed_suppliers