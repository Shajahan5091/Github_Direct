{{ config(
    materialized = 'incremental',
    alias = 'fact_order'
) }}

WITH source_data AS (
    SELECT
        order_id,
        customer_id,
        order_date,
        status,
        total_amount,
        inserted_at
    FROM {{ source('dwh_raw', 'orders') }}
    {% if var('models')['backfill_flag'] == 'true' %}
        WHERE inserted_at BETWEEN '{{ var("models")["backfill_start_date"] }}' AND '{{ var("models")["backfill_end_date"] }}'
    {% else %}
        {% if is_incremental() %}
            WHERE inserted_at > (SELECT MAX(inserted_at) FROM {{ this }})
        {% endif %}
    {% endif %}
),

customer_lookup AS (
    SELECT
        customer_id
    FROM {{ ref('dim_customer') }}
),

validated_orders AS (
    SELECT
        source_orders.order_id,
        source_orders.customer_id,
        source_orders.order_date,
        source_orders.status,
        source_orders.total_amount,
        source_orders.inserted_at
    FROM source_data AS source_orders
    INNER JOIN customer_lookup AS customers
        ON source_orders.customer_id = customers.customer_id
),

transformed AS (
    SELECT
        order_id,
        customer_id,
        DATE(order_date) AS order_date, -- auto corrected using semantic file
        CASE 
            WHEN UPPER(status) IN ('PENDING', 'CANCELLED', 'SHIPPED', 'DELIVERED') 
            THEN UPPER(status)
            ELSE 'PENDING'
        END AS order_status,
        CASE 
            WHEN total_amount < 0 THEN NULL
            ELSE total_amount
        END AS total_amount,
        inserted_at AS last_updated, -- auto corrected using semantic file
        CURRENT_TIMESTAMP() AS last_updated
    FROM validated_orders
)

SELECT * FROM transformed