{{ config(materialized='table') }}

with source_addresses as (
    select
        address_id,
        customer_id,
        address_line1,
        city,
        country,
        inserted_at
    from {{ source('dwh_raw', 'addresses') }}
),

source_customers as (
    select
        customer_id
    from {{ source('dwh_raw', 'customers') }}
),

validated_addresses as (
    select
        addr.address_id,
        addr.customer_id,
        addr.address_line1,
        addr.city,
        addr.country,
        addr.inserted_at
    from source_addresses addr
    inner join source_customers cust
        on addr.customer_id = cust.customer_id
),

transformed_addresses as (
    select
        address_id,
        customer_id,
        trim(address_line1) as address_line1,
        upper(city) as city,
        country,
        date(inserted_at) as inserted_at -- auto corrected using semantic file: converted timestamp to date
    from validated_addresses
)

select * from transformed_addresses