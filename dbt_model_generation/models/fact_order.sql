MODEL NAME    : fact_order
TARGET TABLE  : bsl_ma.dwh_ma.fact_order
SOURCE TABLES : bsl_raw.dwh_raw.orders
DESCRIPTION   : Fact table for order data with normalized status values,
                date conversions, and customer lookups. Includes data
                quality checks and incremental processing.
PREREQUISITES : dim_customer table must exist for customer lookups
PARAMETER     : Incremental materialization with last_updated tracking
AUTHOR        : AI Generated by Shajahan - 2025-11-19
------------------------------------------------------------------------
