WITH source_data AS (
    SELECT
        category_id,
        category_name,
        parent_category_id,
        is_active,
        inserted_at
    FROM {{ source('dwh_raw', 'categories') }}
),

parent_categories AS (
    SELECT
        category_id,
        category_name
    FROM source_data
),

transformed AS (
    SELECT
        source_categories.category_id,
        TRIM(INITCAP(source_categories.category_name)) AS category_name, -- Trim and title-case
        source_categories.parent_category_id,
        CASE 
            WHEN source_categories.parent_category_id IS NOT NULL 
            THEN CONCAT(TRIM(INITCAP(parent_categories.category_name)), ' > ', TRIM(INITCAP(source_categories.category_name)))
            ELSE TRIM(INITCAP(source_categories.category_name))
        END AS category_path, -- Build path up to 2 levels: PARENT_category_name > category_name
        COALESCE(source_categories.is_active, TRUE) AS is_active, -- If null, default true -- auto corrected using semantic file
        source_categories.inserted_at AS last_updated
    FROM source_data AS source_categories
    LEFT JOIN parent_categories 
        ON source_categories.parent_category_id = parent_categories.category_id
)

SELECT * FROM transformed