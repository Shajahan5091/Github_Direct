{#
------------------------------------------------------------------------
MODEL NAME    : fact_order
TARGET TABLE  : bsl_ma.dwh_ma.fact_order
SOURCE TABLES : bsl_raw.dwh_raw.orders
DESCRIPTION   : This model creates the fact_order table by transforming
                raw order data with proper data type conversions,
                status normalization, and customer lookups
PREREQUISITES : dim_customer model must exist for customer validation
PARAMETER     : Incremental model based on order_date
AUTHOR        : AI Generated by Shajahan - 2025-11-17
------------------------------------------------------------------------
#}

{{ config(
    materialized='incremental',
    unique_key='order_id'
) }}

with source_orders as (
    select 
        order_id,
        customer_id,
        order_date,
        status,
        total_amount
    from {{ source('dwh_raw', 'orders') }}
    {% if is_incremental() %}
        where order_date::date > (select coalesce(max(order_date), '1900-01-01') from {{ this }})
    {% endif %}
),

dim_customer_lookup as (
    select 
        customer_id
    from {{ ref('dim_customer') }}
),

transformed_orders as (
    select 
        src_orders.order_id,
        src_orders.customer_id,
        src_orders.order_date::date as order_date,
        case 
            when upper(src_orders.status) in ('PENDING', 'CANCELLED', 'SHIPPED', 'DELIVERED') 
            then upper(src_orders.status)
            else 'PENDING'
        end as order_status,
        case 
            when src_orders.total_amount < 0 then null
            else src_orders.total_amount
        end as total_amount
    from source_orders as src_orders
    inner join dim_customer_lookup as dim_cust
        on src_orders.customer_id = dim_cust.customer_id
)

select 
    transformed_orders.order_id,
    transformed_orders.customer_id,
    transformed_orders.order_date,
    transformed_orders.order_status,
    transformed_orders.total_amount
from transformed_orders