TARGET TABLE  : bsl_ma.dwh_ma.fact_order
SOURCE TABLES : bsl_raw.dwh_raw.orders
DESCRIPTION   : Fact table for order transactions with customer lookup
                and data quality transformations including status
                normalization and incremental loading capability
PREREQUISITES : dim_customer table must exist for customer lookup
PARAMETER     : Incremental loading based on order_date
