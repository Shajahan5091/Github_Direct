{{ config(
    materialized = 'incremental',
    alias = 'fact_shipment'
) }}

WITH source_shipments AS (
    SELECT
        shipment_id,
        order_id,
        shipped_date,
        carrier,
        tracking_number,
        inserted_at
    FROM {{ source('dwh_raw', 'shipments') }}
    {% if var('models')['backfill_flag'] == 'true' %}
        WHERE inserted_at BETWEEN '{{ var("models")["backfill_start_date"] }}' AND '{{ var("models")["backfill_end_date"] }}'
    {% else %}
        {% if is_incremental() %}
            WHERE inserted_at > (SELECT MAX(inserted_at) FROM {{ this }})
        {% endif %}
    {% endif %}
),

source_orders AS (
    SELECT
        order_id,
        order_date
    FROM {{ source('dwh_raw', 'orders') }}
),

shipments_with_orders AS (
    SELECT
        shipments.shipment_id,
        shipments.order_id,
        shipments.shipped_date::DATE AS shipped_date, -- auto corrected using semantic file
        shipments.carrier,
        shipments.tracking_number,
        shipments.inserted_at,
        orders.order_date
    FROM source_shipments AS shipments
    LEFT JOIN source_orders AS orders
        ON shipments.order_id = orders.order_id
),

transformed AS (
    SELECT
        shipment_id,
        order_id,
        shipped_date,
        GREATEST(0, DATEDIFF('day', order_date::DATE, shipped_date)) AS days_to_ship,
        UPPER(carrier) AS carrier,
        tracking_number,
        inserted_at AS last_updated
    FROM shipments_with_orders
    WHERE order_id IS NOT NULL -- Skip if order missing
)

SELECT * FROM transformed