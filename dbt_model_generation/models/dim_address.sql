WITH source_data AS (
    SELECT
        addresses.address_id,
        addresses.customer_id,
        addresses.address_line1,
        addresses.city,
        addresses.country,
        addresses.inserted_at
    FROM {{ source('dwh_raw', 'addresses') }} AS addresses
),

customer_lookup AS (
    SELECT
        dim_customer.customer_id
    FROM {{ ref('dim_customer') }} AS dim_customer
),

validated_addresses AS (
    SELECT
        source_addresses.address_id,
        source_addresses.customer_id,
        source_addresses.address_line1,
        source_addresses.city,
        source_addresses.country,
        source_addresses.inserted_at
    FROM source_data AS source_addresses
    INNER JOIN customer_lookup AS customers
        ON source_addresses.customer_id = customers.customer_id
),

transformed AS (
    SELECT
        validated_addresses.address_id,
        validated_addresses.customer_id,
        TRIM(validated_addresses.address_line1) AS address_line1,
        UPPER(validated_addresses.city) AS city,
        validated_addresses.country,
        validated_addresses.inserted_at AS last_updated -- auto corrected using semantic file
    FROM validated_addresses
)

SELECT * FROM transformed