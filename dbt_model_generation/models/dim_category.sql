{{ config(materialized='table') }}

with source_categories as (
    select
        categories.category_id,
        categories.category_name,
        categories.parent_category_id,
        categories.is_active,
        categories.inserted_at
    from {{ source('dwh_raw', 'categories') }} as categories
),

parent_categories as (
    select
        parent_cat.category_id as parent_category_id,
        trim(initcap(parent_cat.category_name)) as parent_category_name
    from {{ source('dwh_raw', 'categories') }} as parent_cat
),

transformed_categories as (
    select
        source_categories.category_id,
        trim(initcap(source_categories.category_name)) as category_name,
        source_categories.parent_category_id,
        case 
            when source_categories.parent_category_id is not null and parent_categories.parent_category_name is not null
            then parent_categories.parent_category_name || ' > ' || trim(initcap(source_categories.category_name))
            else trim(initcap(source_categories.category_name))
        end as category_path,
        coalesce(source_categories.is_active, true) as is_active,
        source_categories.inserted_at as last_updated
    from source_categories
    left join parent_categories 
        on source_categories.parent_category_id = parent_categories.parent_category_id
)

select
    transformed_categories.category_id,
    transformed_categories.category_name,
    transformed_categories.parent_category_id,
    transformed_categories.category_path,
    transformed_categories.is_active,
    transformed_categories.last_updated
from transformed_categories