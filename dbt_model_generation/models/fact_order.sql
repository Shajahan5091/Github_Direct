{{ config(
    materialized='incremental',
    unique_key='order_id'
) }}

with source_orders as (
    select 
        order_id,
        customer_id,
        order_date,
        status,
        total_amount
    from {{ source('dwh_raw', 'orders') }}
    {% if is_incremental() %}
        where order_date > (select max(order_date) from {{ this }})
    {% endif %}
),

customer_lookup as (
    select 
        customer_id
    from {{ ref('dim_customer') }}
),

validated_orders as (
    select 
        src.order_id,
        src.customer_id,
        src.order_date::date as order_date,
        case 
            when upper(src.status) in ('PENDING', 'CANCELLED', 'SHIPPED', 'DELIVERED') 
            then upper(src.status)
            else 'PENDING'
        end as order_status,
        case 
            when src.total_amount < 0 then null
            else src.total_amount
        end as total_amount,
        current_timestamp() as last_updated
    from source_orders src
    inner join customer_lookup cust
        on src.customer_id = cust.customer_id
)

select 
    order_id,
    customer_id,
    order_date,
    order_status,
    total_amount,
    last_updated
from validated_orders