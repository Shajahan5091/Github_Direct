WITH source_order_items AS (
    SELECT
        order_items.order_item_id,
        order_items.order_id,
        order_items.product_id,
        order_items.quantity,
        order_items.unit_price,
        order_items.inserted_at
    FROM {{ source('dwh_raw', 'order_items') }} AS order_items
    {% if var('models')['backfill_flag'] == 'true' %}
        WHERE order_items.inserted_at BETWEEN '{{ var("models")["backfill_start_date"] }}' AND '{{ var("models")["backfill_end_date"] }}'
    {% else %}
        {% if is_incremental() %}
            WHERE order_items.inserted_at > (SELECT MAX(inserted_at) FROM {{ this }})
        {% endif %}
    {% endif %}
),

source_orders AS (
    SELECT
        orders.order_id,
        orders.customer_id,
        orders.order_date,
        orders.status,
        orders.total_amount
    FROM {{ source('dwh_raw', 'orders') }} AS orders
),

order_items_with_orders AS (
    SELECT
        source_order_items.order_item_id,
        source_order_items.order_id,
        source_order_items.product_id,
        source_order_items.quantity,
        source_order_items.unit_price,
        source_order_items.inserted_at,
        source_orders.customer_id,
        source_orders.order_date,
        source_orders.status,
        source_orders.total_amount
    FROM source_order_items
    INNER JOIN source_orders
        ON source_order_items.order_id = source_orders.order_id
),

-- auto corrected using semantic file: ensuring integer semantics for quantity
order_items_with_calculations AS (
    SELECT
        order_items_with_orders.order_item_id,
        order_items_with_orders.order_id,
        -- If product missing set product_id to 0
        CASE 
            WHEN order_items_with_orders.product_id IS NULL THEN '0'
            ELSE order_items_with_orders.product_id
        END AS product_id,
        -- Ensure integer semantics; round if fractional
        ROUND(order_items_with_orders.quantity, 0) AS quantity,
        -- If unit_price null treat as 0
        COALESCE(order_items_with_orders.unit_price, 0) AS unit_price,
        -- Compute quantity * unit_price for extended_amount
        ROUND(order_items_with_orders.quantity, 0) * COALESCE(order_items_with_orders.unit_price, 0) AS extended_amount,
        -- Set 0 if no discount context
        0.00 AS discount_amount,
        order_items_with_orders.inserted_at
    FROM order_items_with_orders
),

final_transformations AS (
    SELECT
        order_items_with_calculations.order_item_id,
        order_items_with_calculations.order_id,
        order_items_with_calculations.product_id,
        order_items_with_calculations.quantity,
        order_items_with_calculations.unit_price::NUMBER(10,2) AS unit_price,
        order_items_with_calculations.extended_amount::NUMBER(12,2) AS extended_amount,
        order_items_with_calculations.discount_amount::NUMBER(12,2) AS discount_amount,
        -- Compute extended_amount - discount_amount; ensure non-negative; floor at 0
        GREATEST(
            order_items_with_calculations.extended_amount - order_items_with_calculations.discount_amount, 
            0
        )::NUMBER(12,2) AS net_amount,
        order_items_with_calculations.inserted_at AS last_updated,
        CURRENT_TIMESTAMP() AS last_updated
    FROM order_items_with_calculations
)

SELECT * FROM final_transformations