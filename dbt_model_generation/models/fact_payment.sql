{{ config(
    materialized = 'incremental',
    unique_key = 'payment_id',
    incremental_strategy = 'merge'
) }}

WITH source_payments AS (
    SELECT
        payment_id,
        order_id,
        payment_date,
        payment_method,
        amount,
        inserted_at
    FROM {{ source('dwh_raw', 'payments') }}
    {% if var('backfill_flag') == 'true' %}
        WHERE inserted_at BETWEEN '{{ var("backfill_start_date") }}' AND '{{ var("backfill_end_date") }}'
    {% else %}
        {% if is_incremental() %}
            WHERE inserted_at > COALESCE(
                (SELECT MAX(last_updated) FROM BSL_MA.DWH_MA.fact_payment), 
                TO_DATE('0001-01-01')
            )
        {% endif %}
    {% endif %}
),

source_orders AS (
    SELECT
        order_id,
        total_amount
    FROM {{ ref('fact_order') }}
),

validated_payments AS (
    SELECT
        payments.payment_id,
        payments.order_id,
        payments.payment_date::DATE AS payment_date, -- auto corrected using semantic file
        UPPER(
            CASE 
                WHEN UPPER(payments.payment_method) IN ('CREDIT CARD', 'DEBIT CARD') THEN 'CARD'
                WHEN UPPER(payments.payment_method) = 'PAYPAL' THEN 'UPI'
                WHEN UPPER(payments.payment_method) IN ('COD', 'NETBANKING') THEN payments.payment_method
                ELSE 'CARD'
            END
        ) AS payment_method,
        CASE 
            WHEN payments.amount < 0 THEN NULL
            ELSE payments.amount
        END AS amount,
        payments.inserted_at AS last_updated
    FROM source_payments AS payments
    INNER JOIN source_orders AS orders
        ON payments.order_id = orders.order_id
),

payment_aggregation AS (
    SELECT
        order_id,
        SUM(COALESCE(amount, 0)) AS total_payments
    FROM validated_payments
    GROUP BY order_id
),

final_payments AS (
    SELECT
        validated_payments.payment_id,
        validated_payments.order_id,
        validated_payments.payment_date,
        validated_payments.payment_method,
        validated_payments.amount,
        CASE 
            WHEN orders.total_amount IS NULL THEN FALSE
            WHEN payment_aggregation.total_payments >= orders.total_amount THEN TRUE
            ELSE FALSE
        END AS is_fully_applied,
        validated_payments.last_updated
    FROM validated_payments
    LEFT JOIN source_orders AS orders
        ON validated_payments.order_id = orders.order_id
    LEFT JOIN payment_aggregation
        ON validated_payments.order_id = payment_aggregation.order_id
)

SELECT * FROM final_payments