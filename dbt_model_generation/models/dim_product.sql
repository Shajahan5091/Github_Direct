TARGET TABLE  : BSL_MA.DWH_MA.DIM_PRODUCT
SOURCE TABLES : BSL_RAW.DWH_RAW.PRODUCTS
DESCRIPTION   : Dimension table for products with price banding and 
                standardized product names
PREREQUISITES : Source table PRODUCTS must exist in BSL_RAW.DWH_RAW
PARAMETER     : None
AUTHOUR       : AI Generated by Shajahan - 2025-11-19
------------------------------------------------------------------------
#}

{{ config(
    materialized='table'
) }}

with source_products as (
    select
        product_id,
        product_name,
        price,
        inserted_at
    from {{ source('dwh_raw', 'products') }}
),

transformed_products as (
    select
        products.product_id,
        -- Title-case product name and trim excessive spaces
        trim(regexp_replace(initcap(products.product_name), '\\s+', ' ')) as product_name,
        products.price,
        -- Price banding logic
        case
            when products.price < 50 then 'Low'
            when products.price >= 50 and products.price <= 199.99 then 'Mid'
            when products.price >= 200 and products.price <= 499.99 then 'High'
            when products.price >= 500 then 'Premium'
            else 'Unknown'
        end as price_band,
        products.inserted_at,
        current_timestamp() as last_updated
    from source_products as products
)

select
    product_id,
    product_name,
    price,
    price_band,
    inserted_at,
    last_updated
from transformed_products