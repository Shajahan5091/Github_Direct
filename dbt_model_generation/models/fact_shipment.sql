{{ config(
    materialized='incremental'
) }}

with source_shipments as (
    select
        shipment_id,
        order_id,
        shipped_date,
        carrier,
        tracking_number
    from {{ source('dwh_raw', 'shipments') }}
    {% if var('models')['backfill_flag'] == 'true' %}
        where shipped_date between '{{ var("models")["backfill_start_date"] }}' and '{{ var("models")["backfill_end_date"] }}'
    {% endif %}
    {% if is_incremental() %}
        where shipped_date > (select max(shipped_date) from {{ this }})
    {% endif %}
),

source_orders as (
    select
        order_id,
        order_date
    from {{ source('dwh_raw', 'orders') }}
),

shipment_with_order_details as (
    select
        shipments.shipment_id,
        shipments.order_id,
        shipments.shipped_date::date as shipped_date,
        greatest(0, datediff('day', orders.order_date::date, shipments.shipped_date::date)) as days_to_ship, -- auto corrected using semantic file
        upper(shipments.carrier) as carrier,
        shipments.tracking_number,
        current_timestamp() as last_updated
    from source_shipments as shipments
    inner join source_orders as orders
        on shipments.order_id = orders.order_id
)

select
    shipment_id,
    order_id,
    shipped_date,
    days_to_ship,
    carrier,
    tracking_number,
    last_updated
from shipment_with_order_details