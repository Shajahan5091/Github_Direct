{{ config(materialized='table') }}

with source_products as (
    select * from {{ source('dwh_raw', 'products') }}
),

transformed_products as (
    select
        products.product_id,
        initcap(trim(products.product_name)) as product_name,
        products.price,
        case
            when products.price < 50 then 'Low'
            when products.price >= 50 and products.price <= 199.99 then 'Mid'
            when products.price >= 200 and products.price <= 499.99 then 'High'
            when products.price >= 500 then 'Premium'
            else 'Unknown'
        end as price_band
    from source_products as products
)

select * from transformed_products