{{ config(
    materialized = 'incremental',
    unique_key = 'payment_id',
    incremental_strategy = 'merge'
) }}

WITH source_payments AS (
    SELECT
        payment_id,
        order_id,
        payment_date,
        payment_method,
        amount,
        inserted_at
    FROM {{ source('dwh_raw', 'payments') }}
    {% if var('backfill_flag') == 'true' %}
        WHERE inserted_at BETWEEN '{{ var("backfill_start_date") }}' AND '{{ var("backfill_end_date") }}'
    {% else %}
        {% if is_incremental() %}
            WHERE inserted_at > COALESCE(
                (SELECT MAX(last_updated) FROM BSL_MA.DWH_MA.fact_payment), 
                TO_DATE('0001-01-01')
            )
        {% endif %}
    {% endif %}
),

source_orders AS (
    SELECT
        order_id,
        total_amount
    FROM {{ source('dwh_raw', 'orders') }}
),

-- Join payments with orders to get order total for is_fully_applied calculation
payments_with_orders AS (
    SELECT
        payments.payment_id,
        payments.order_id,
        payments.payment_date,
        payments.payment_method,
        payments.amount,
        payments.inserted_at,
        orders.total_amount AS order_total_amount
    FROM source_payments AS payments
    LEFT JOIN source_orders AS orders
        ON payments.order_id = orders.order_id
    -- Skip payment if order not present
    WHERE orders.order_id IS NOT NULL
),

-- Calculate payment aggregations per order for is_fully_applied logic
order_payment_totals AS (
    SELECT
        order_id,
        SUM(amount) AS total_payments_for_order
    FROM payments_with_orders
    GROUP BY order_id
),

transformed AS (
    SELECT
        payments.payment_id,
        payments.order_id,
        DATE(payments.payment_date) AS payment_date, -- Convert timestamp to date
        CASE 
            WHEN UPPER(payments.payment_method) IN ('CREDIT CARD', 'DEBIT CARD') THEN 'CARD'
            WHEN UPPER(payments.payment_method) = 'PAYPAL' THEN 'UPI'
            WHEN UPPER(payments.payment_method) IN ('COD', 'CASH ON DELIVERY') THEN 'COD'
            WHEN UPPER(payments.payment_method) IN ('NET BANKING', 'NETBANKING') THEN 'NETBANKING'
            ELSE 'CARD' -- If not in list set to 'CARD' -- auto corrected using semantic file
        END AS payment_method,
        CASE 
            WHEN payments.amount < 0 THEN NULL -- If negative set to null and log
            ELSE payments.amount
        END AS amount,
        CASE 
            WHEN payments.order_total_amount IS NULL THEN FALSE -- If order total null set false
            WHEN order_totals.total_payments_for_order >= payments.order_total_amount THEN TRUE
            ELSE FALSE
        END AS is_fully_applied,
        payments.inserted_at AS last_updated -- Direct mapping
    FROM payments_with_orders AS payments
    LEFT JOIN order_payment_totals AS order_totals
        ON payments.order_id = order_totals.order_id
)

SELECT * FROM transformed