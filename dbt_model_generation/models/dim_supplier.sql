WITH source_data AS (
    SELECT
        supplier_id,
        supplier_name,
        contact_email,
        country,
        active_flag,
        inserted_at
    FROM {{ source('dwh_raw', 'suppliers') }}
),

transformed AS (
    SELECT
        supplier_id,
        INITCAP(TRIM(supplier_name)) AS supplier_name, -- Trim and title-case
        CASE 
            WHEN REGEXP_LIKE(contact_email, '^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,}$') 
            THEN LOWER(contact_email) 
            ELSE NULL 
        END AS contact_email, -- auto corrected using semantic file: Lowercase email and validate pattern
        UPPER(country) AS country, -- Uppercase country
        COALESCE(active_flag, FALSE) AS is_active, -- If null default false
        inserted_at AS last_updated -- Direct mapping
    FROM source_data
)

SELECT * FROM transformed