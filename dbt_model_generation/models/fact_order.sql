{{ config(
    materialized='incremental',
    unique_key='order_id'
) }}

with source_orders as (
    select
        orders.order_id,
        orders.customer_id,
        orders.order_date,
        orders.status,
        orders.total_amount
    from {{ source('dwh_raw', 'orders') }} as orders
    {% if var('models')['backfill_flag'] == 'true' %}
        where orders.order_date between '{{ var("models")["backfill_start_date"] }}' and '{{ var("models")["backfill_end_date"] }}'
    {% endif %}
    {% if is_incremental() %}
        where orders.order_date > (select max(order_date) from {{ this }})
    {% endif %}
),

transformed_orders as (
    select
        source_orders.order_id,
        source_orders.customer_id,
        source_orders.order_date::date as order_date,
        case 
            when upper(source_orders.status) in ('PENDING', 'CANCELLED', 'SHIPPED', 'DELIVERED') 
            then upper(source_orders.status)
            else 'PENDING' -- auto corrected using semantic file: default value for invalid status
        end as order_status,
        case 
            when source_orders.total_amount < 0 then null
            else source_orders.total_amount
        end as total_amount,
        current_timestamp() as last_updated
    from source_orders
    where source_orders.customer_id is not null -- Skip orders without matching customer
)

select
    transformed_orders.order_id,
    transformed_orders.customer_id,
    transformed_orders.order_date,
    transformed_orders.order_status,
    transformed_orders.total_amount,
    transformed_orders.last_updated
from transformed_orders