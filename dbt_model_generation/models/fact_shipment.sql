TARGET TABLE  : BSL_MA.DWH_MA.FACT_SHIPMENT
SOURCE TABLES : BSL_RAW.DWH_RAW.SHIPMENTS, BSL_RAW.DWH_RAW.ORDERS
DESCRIPTION   : This model creates a fact table for shipment data with
                enriched information including days to ship calculation
                and order details lookup.
PREREQUISITES : Orders data must be available in source system
PARAMETER     : backfill_flag, backfill_start_date, backfill_end_date
