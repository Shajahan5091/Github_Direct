with source_payments as (
    select 
        payment_id,
        order_id,
        payment_date,
        payment_method,
        amount
    from {{ source('dwh_raw', 'payments') }}
),

source_orders as (
    select 
        order_id,
        total_amount
    from {{ source('dwh_raw', 'orders') }}
),

-- Auto corrected using semantic file: Changed payment_method mapping logic
payment_method_standardized as (
    select 
        payments.payment_id,
        payments.order_id,
        payments.payment_date::date as payment_date,
        case 
            when upper(payments.payment_method) in ('CREDIT CARD', 'DEBIT CARD') then 'CARD'
            when upper(payments.payment_method) = 'PAYPAL' then 'UPI'
            when upper(payments.payment_method) = 'CASH ON DELIVERY' then 'COD'
            when upper(payments.payment_method) = 'NET BANKING' then 'NETBANKING'
            else 'CARD'
        end as payment_method,
        case 
            when payments.amount < 0 then null
            else payments.amount
        end as amount,
        orders.total_amount as order_total_amount
    from source_payments as payments
    inner join source_orders as orders
        on payments.order_id = orders.order_id
),

payment_aggregation as (
    select 
        order_id,
        sum(coalesce(amount, 0)) as total_payments
    from payment_method_standardized
    group by order_id
),

final_payment_facts as (
    select 
        pms.payment_id,
        pms.order_id,
        pms.payment_date,
        pms.payment_method,
        pms.amount,
        case 
            when pms.order_total_amount is null then false
            when pa.total_payments >= pms.order_total_amount then true
            else false
        end as is_fully_applied,
        current_timestamp() as last_updated
    from payment_method_standardized as pms
    left join payment_aggregation as pa
        on pms.order_id = pa.order_id
)

select * from final_payment_facts

