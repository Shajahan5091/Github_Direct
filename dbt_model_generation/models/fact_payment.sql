{{ config(
    materialized='incremental',
    unique_key='payment_id',
    incremental_strategy='merge'
) }}

WITH source_payments AS (
    SELECT
        payment_id,
        order_id,
        payment_date,
        payment_method,
        amount,
        inserted_at
    FROM {{ source('dwh_raw', 'payments') }}
    {% if var('backfill_flag') == 'true' %}
        WHERE inserted_at BETWEEN '{{ var("backfill_start_date") }}' AND '{{ var("backfill_end_date") }}'
    {% else %}
        {% if is_incremental() %}
            WHERE inserted_at > COALESCE(
                (SELECT MAX(last_updated) FROM BSL_MA.DWH_MA.fact_payment), 
                TO_DATE('0001-01-01')
            )
        {% endif %}
    {% endif %}
),

source_orders AS (
    SELECT
        order_id,
        total_amount
    FROM {{ source('dwh_raw', 'orders') }}
),

validated_payments AS (
    SELECT
        payments.payment_id,
        payments.order_id,
        payments.payment_date::DATE AS payment_date, -- auto corrected using semantic file
        CASE 
            WHEN UPPER(payments.payment_method) IN ('CREDIT CARD', 'DEBIT CARD') THEN 'CARD'
            WHEN UPPER(payments.payment_method) = 'PAYPAL' THEN 'UPI'
            WHEN UPPER(payments.payment_method) IN ('COD', 'NETBANKING') THEN UPPER(payments.payment_method)
            ELSE 'CARD'
        END AS payment_method,
        CASE 
            WHEN payments.amount < 0 THEN NULL
            ELSE payments.amount
        END AS amount,
        orders.total_amount AS order_total_amount,
        payments.inserted_at AS last_updated
    FROM source_payments AS payments
    INNER JOIN source_orders AS orders
        ON payments.order_id = orders.order_id
),

payment_aggregation AS (
    SELECT
        order_id,
        SUM(COALESCE(amount, 0)) AS total_payments
    FROM validated_payments
    GROUP BY order_id
),

final_payments AS (
    SELECT
        payments.payment_id,
        payments.order_id,
        payments.payment_date,
        payments.payment_method,
        payments.amount::NUMBER(10,2) AS amount, -- auto corrected using semantic file
        CASE 
            WHEN payments.order_total_amount IS NULL THEN FALSE
            WHEN agg.total_payments >= payments.order_total_amount THEN TRUE
            ELSE FALSE
        END AS is_fully_applied,
        payments.last_updated
    FROM validated_payments AS payments
    LEFT JOIN payment_aggregation AS agg
        ON payments.order_id = agg.order_id
)

SELECT * FROM final_payments