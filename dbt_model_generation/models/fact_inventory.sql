{{ config(
    materialized='table',
    unique_key='product_id||supplier_id||snapshot_date',
    incremental_strategy='merge'
) }}

WITH source_data AS (
    SELECT
        inventory.product_id,
        inventory.supplier_id,
        inventory.stock_qty,
        inventory.warehouse_location,
        inventory.last_updated,
        inventory.inserted_at
    FROM {{ source('dwh_raw', 'inventory') }} AS inventory
    {% if var('backfill_flag') == 'true' %}
        WHERE inventory.inserted_at BETWEEN '{{ var("backfill_start_date") }}' AND '{{ var("backfill_end_date") }}'
    {% else %}
        {% if is_incremental() %}
            WHERE inventory.inserted_at > COALESCE(
                (SELECT MAX(last_updated) FROM BSL_MA.DWH_MA.fact_inventory), 
                TO_DATE('0001-01-01')
            )
        {% endif %}
    {% endif %}
),

product_lookup AS (
    SELECT
        product_details.product_id
    FROM {{ ref('dim_product') }} AS product_details
),

supplier_lookup AS (
    SELECT
        supplier_details.supplier_id
    FROM {{ ref('dim_supplier') }} AS supplier_details
),

latest_snapshot_per_product_supplier AS (
    SELECT
        source_inventory.product_id,
        source_inventory.supplier_id,
        MAX(source_inventory.last_updated) AS max_last_updated
    FROM source_data AS source_inventory
    GROUP BY 
        source_inventory.product_id,
        source_inventory.supplier_id
),

transformed AS (
    SELECT
        -- Auto corrected using semantic file: product_id lookup with fallback to '0' for unknown
        COALESCE(product_lookup.product_id, source_inventory.product_id, '0') AS product_id,
        
        -- Auto corrected using semantic file: supplier_id lookup with fallback to '0' for unknown  
        COALESCE(supplier_lookup.supplier_id, source_inventory.supplier_id, '0') AS supplier_id,
        
        -- Stock quantity with null handling for negative values
        CASE 
            WHEN source_inventory.stock_qty < 0 THEN NULL
            ELSE source_inventory.stock_qty
        END AS stock_qty,
        
        -- Uppercase warehouse location
        UPPER(source_inventory.warehouse_location) AS warehouse_location,
        
        -- Convert timestamp to date for snapshot_date
        DATE(source_inventory.last_updated) AS snapshot_date,
        
        -- Flag latest record per product_id + supplier_id combination
        CASE 
            WHEN source_inventory.last_updated = latest_snapshot.max_last_updated THEN TRUE
            ELSE FALSE
        END AS is_latest,
        
        -- Direct mapping for last_updated from inserted_at
        source_inventory.inserted_at AS last_updated
        
    FROM source_data AS source_inventory
    LEFT JOIN product_lookup 
        ON source_inventory.product_id = product_lookup.product_id
    LEFT JOIN supplier_lookup 
        ON source_inventory.supplier_id = supplier_lookup.supplier_id
    LEFT JOIN latest_snapshot_per_product_supplier AS latest_snapshot
        ON source_inventory.product_id = latest_snapshot.product_id
        AND source_inventory.supplier_id = latest_snapshot.supplier_id
)

SELECT * FROM transformed