MODEL NAME    : fact_order
TARGET TABLE  : bsl_ma.dwh_ma.fact_order
SOURCE TABLES : bsl_raw.dwh_raw.orders
DESCRIPTION   : Fact table for order data with customer lookup and data 
                transformations including status normalization and date 
                conversion
PREREQUISITES : dim_customer table must exist for customer lookup
PARAMETER     : Incremental model based on order_date
AUTHOUR       : AI Generated by Shajahan - 2025-11-17
------------------------------------------------------------------------
#}

{{ config(
    materialized='incremental',
    unique_key='order_id'
) }}

with source_orders as (
    select 
        order_id,
        customer_id,
        order_date,
        status,
        total_amount
    from {{ source('dwh_raw', 'orders') }}
    {% if is_incremental() %}
        where order_date::date > (select coalesce(max(order_date), '1900-01-01') from {{ this }})
    {% endif %}
),

dim_customer_lookup as (
    select 
        customer_id
    from {{ ref('dim_customer') }}
),

transformed_orders as (
    select 
        source_orders.order_id,
        source_orders.customer_id,
        source_orders.order_date::date as order_date,
        case 
            when upper(source_orders.status) in ('PENDING', 'CANCELLED', 'SHIPPED', 'DELIVERED') 
            then upper(source_orders.status)
            else 'PENDING'
        end as order_status,
        case 
            when source_orders.total_amount < 0 
            then null
            else source_orders.total_amount
        end as total_amount
    from source_orders
    inner join dim_customer_lookup 
        on source_orders.customer_id = dim_customer_lookup.customer_id
)

select 
    order_id,
    customer_id,
    order_date,
    order_status,
    total_amount
from transformed_orders