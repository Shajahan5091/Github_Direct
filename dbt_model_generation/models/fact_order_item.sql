MODEL NAME    : fact_order_item
TARGET TABLE  : bsl_ma.dwh_ma.fact_order_item
SOURCE TABLES : bsl_raw.dwh_raw.order_items, bsl_raw.dwh_raw.orders
DESCRIPTION   : Fact table for order items with calculated amounts and 
                discount allocations. Includes extended amounts, discount
                amounts, and net amounts for each order item.
PREREQUISITES : dim_product and fact_order tables must exist
PARAMETER     : Incremental load based on last_updated timestamp
AUTHOUR       : AI Generated by Shajahan - 2025-11-18
------------------------------------------------------------------------
#}

{{ config(
    materialized='incremental',
    unique_key='order_item_id'
) }}

with source_order_items as (
    select
        order_item_id,
        order_id,
        product_id,
        quantity,
        unit_price
    from {{ source('dwh_raw', 'order_items') }}
    {% if is_incremental() %}
        where last_updated > (select max(last_updated) from {{ this }})
    {% endif %}
),

source_orders as (
    select
        order_id
    from {{ source('dwh_raw', 'orders') }}
),

validated_order_items as (
    select
        oi.order_item_id,
        oi.order_id,
        case 
            when dp.product_id is null then '0'
            else oi.product_id
        end as product_id,
        round(oi.quantity) as quantity,
        coalesce(oi.unit_price, 0) as unit_price
    from source_order_items oi
    left join {{ ref('dim_product') }} dp
        on oi.product_id = dp.product_id
    inner join {{ ref('fact_order') }} fo
        on oi.order_id = fo.order_id
),

calculated_amounts as (
    select
        voi.order_item_id,
        voi.order_id,
        voi.product_id,
        voi.quantity,
        voi.unit_price,
        (voi.quantity * voi.unit_price) as extended_amount,
        coalesce(0, 0) as discount_amount
    from validated_order_items voi
),

final_calculations as (
    select
        ca.order_item_id,
        ca.order_id,
        ca.product_id,
        ca.quantity,
        ca.unit_price,
        ca.extended_amount,
        ca.discount_amount,
        greatest(0, ca.extended_amount - ca.discount_amount) as net_amount,
        current_timestamp() as last_updated
    from calculated_amounts ca
)

select
    order_item_id,
    order_id,
    product_id,
    quantity,
    unit_price,
    extended_amount,
    discount_amount,
    net_amount,
    last_updated
from final_calculations