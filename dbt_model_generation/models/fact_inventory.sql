{{ config(
    materialized = 'table',
    alias = 'fact_inventory'
) }}

WITH source_data AS (
    SELECT
        inventory.product_id,
        inventory.supplier_id,
        inventory.stock_qty,
        inventory.warehouse_location,
        inventory.last_updated,
        inventory.inserted_at -- auto corrected using semantic file
    FROM {{ source('dwh_raw', 'inventory') }}
    {% if var('models')['backfill_flag'] == 'true' %}
        WHERE inventory.inserted_at BETWEEN 'var("models")["backfill_start_date"]' AND 'var("models")["backfill_end_date"]'
    {% else %}
        {% if is_incremental() %}
            WHERE inventory.inserted_at > (SELECT MAX(inserted_at) FROM {{ this }})
        {% endif %}
    {% endif %}
),

product_lookup AS (
    SELECT
        product_id
    FROM {{ ref('dim_product') }}
),

supplier_lookup AS (
    SELECT
        supplier_id
    FROM {{ ref('dim_supplier') }}
),

transformed AS (
    SELECT
        -- Product ID with unknown handling
        COALESCE(product_lookup.product_id, source_data.product_id, '0') AS product_id, -- auto corrected using semantic file: If missing set to 0 (unknown) and retain for audit
        
        -- Supplier ID with unknown handling
        COALESCE(supplier_lookup.supplier_id, source_data.supplier_id, '0') AS supplier_id, -- auto corrected using semantic file: If supplier missing set to 0 (unknown)
        
        -- Stock quantity with negative value handling
        CASE 
            WHEN source_data.stock_qty < 0 THEN NULL -- auto corrected using semantic file: If negative set to null and flag
            ELSE source_data.stock_qty
        END AS stock_qty,
        
        -- Warehouse location uppercase transformation
        UPPER(source_data.warehouse_location) AS warehouse_location,
        
        -- Convert timestamp to date for snapshot
        DATE(source_data.last_updated) AS snapshot_date,
        
        -- Flag latest record per product_id + supplier_id
        CASE 
            WHEN source_data.last_updated = MAX(source_data.last_updated) OVER (
                PARTITION BY source_data.product_id, source_data.supplier_id
            ) THEN TRUE
            ELSE FALSE
        END AS is_latest,
        
        -- Direct mapping for last_updated timestamp
        source_data.inserted_at AS last_updated,
        
        -- Add current timestamp for incremental materialization
        CURRENT_TIMESTAMP() AS last_updated_timestamp
        
    FROM source_data
    LEFT JOIN product_lookup
        ON source_data.product_id = product_lookup.product_id
    LEFT JOIN supplier_lookup
        ON source_data.supplier_id = supplier_lookup.supplier_id
)

SELECT * FROM transformed