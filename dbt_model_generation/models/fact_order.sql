MODEL NAME    : fact_order
TARGET TABLE  : BSL_MA.DWH_MA.FACT_ORDER
SOURCE TABLES : BSL_RAW.DWH_RAW.ORDERS
DESCRIPTION   : This model creates a fact table for orders with normalized 
                status values, date conversions, and customer lookups. It 
                includes incremental processing for efficient data loading.
PREREQUISITES : dim_customer table must exist for customer lookups
PARAMETER     : backfill_flag, backfill_start_date, backfill_end_date
AUTHOUR       : AI Generated by Shajahan - 2025-11-27
------------------------------------------------------------------------
#}

{{ config(
    materialized='incremental',
    unique_key='order_id',
    incremental_strategy='merge'
) }}

WITH source_orders AS (
    SELECT
        order_id,
        customer_id,
        order_date,
        status,
        total_amount,
        inserted_at
    FROM {{ source('dwh_raw', 'orders') }}
    {% if var('backfill_flag') == 'true' %}
        WHERE inserted_at BETWEEN '{{ var("backfill_start_date") }}' AND '{{ var("backfill_end_date") }}'
    {% else %}
        {% if is_incremental() %}
            WHERE inserted_at > COALESCE(
                (SELECT MAX(last_updated) FROM BSL_MA.DWH_MA.fact_order), 
                TO_DATE('0001-01-01')
            )
        {% endif %}
    {% endif %}
),

customer_lookup AS (
    SELECT
        customer_id
    FROM {{ ref('dim_customer') }}
),

validated_orders AS (
    SELECT
        source_orders.order_id,
        source_orders.customer_id,
        source_orders.order_date,
        source_orders.status,
        source_orders.total_amount,
        source_orders.inserted_at
    FROM source_orders
    INNER JOIN customer_lookup
        ON source_orders.customer_id = customer_lookup.customer_id
),

transformed AS (
    SELECT
        order_id,
        customer_id,
        DATE(order_date) AS order_date, -- auto corrected using semantic file
        CASE 
            WHEN UPPER(status) IN ('PENDING', 'CANCELLED', 'SHIPPED', 'DELIVERED') 
            THEN UPPER(status)
            ELSE 'PENDING'
        END AS order_status,
        CASE 
            WHEN total_amount < 0 THEN NULL
            ELSE total_amount
        END AS total_amount,
        inserted_at AS last_updated
    FROM validated_orders
)

SELECT * FROM transformed