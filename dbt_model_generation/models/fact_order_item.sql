MODEL NAME    : fact_order_item
TARGET TABLE  : bsl_ma.dwh_ma.fact_order_item
SOURCE TABLES : bsl_raw.dwh_raw.order_items, bsl_raw.dwh_raw.orders
DESCRIPTION   : Fact table for order items with calculated extended amounts,
                discount allocations, and net amounts. Includes lookups to
                validate order and product references.
PREREQUISITES : fact_order and dim_product models must exist
PARAMETER     : Incremental model with conditional loading
AUTHOR        : AI Generated by Shajahan - 2025-11-17
------------------------------------------------------------------------
#}

{{ config(
    materialized='incremental',
    unique_key='order_item_id'
) }}

with source_order_items as (
    select
        order_item_id,
        order_id,
        product_id,
        quantity,
        unit_price
    from {{ source('dwh_raw', 'order_items') }}
    {% if is_incremental() %}
        where exists (select 1 where true) and {{ is_incremental() }}
    {% endif %}
),

source_orders as (
    select
        order_id
    from {{ source('dwh_raw', 'orders') }}
),

fact_order_lookup as (
    select
        order_id
    from {{ ref('fact_order') }}
),

dim_product_lookup as (
    select
        product_id
    from {{ ref('dim_product') }}
),

validated_order_items as (
    select
        source_items.order_item_id,
        source_items.order_id,
        case 
            when dim_products.product_id is null then '0'
            else source_items.product_id
        end as product_id,
        round(source_items.quantity) as quantity,
        source_items.unit_price
    from source_order_items as source_items
    inner join fact_order_lookup as fact_orders
        on source_items.order_id = fact_orders.order_id
    left join dim_product_lookup as dim_products
        on source_items.product_id = dim_products.product_id
),

calculated_amounts as (
    select
        validated_items.order_item_id,
        validated_items.order_id,
        validated_items.product_id,
        validated_items.quantity,
        validated_items.unit_price,
        (validated_items.quantity * coalesce(validated_items.unit_price, 0))::number(12,2) as extended_amount,
        0::number(12,2) as discount_amount
    from validated_order_items as validated_items
),

final_calculations as (
    select
        calc_amounts.order_item_id,
        calc_amounts.order_id,
        calc_amounts.product_id,
        calc_amounts.quantity,
        calc_amounts.unit_price::number(10,2) as unit_price,
        calc_amounts.extended_amount,
        calc_amounts.discount_amount,
        greatest(0, calc_amounts.extended_amount - calc_amounts.discount_amount)::number(12,2) as net_amount
    from calculated_amounts as calc_amounts
)

select
    final_calc.order_item_id,
    final_calc.order_id,
    final_calc.product_id,
    final_calc.quantity,
    final_calc.unit_price,
    final_calc.extended_amount,
    final_calc.discount_amount,
    final_calc.net_amount
from final_calculations as final_calc