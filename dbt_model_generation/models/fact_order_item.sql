{{ config(
    materialized='incremental',
    unique_key='order_item_id'
) }}

with order_items_source as (
    select 
        order_item_id,
        order_id,
        product_id,
        quantity,
        unit_price,
        inserted_at
    from {{ source('dwh_raw', 'order_items') }}
    {% if var('models')['backfill_flag'] == 'true' %}
        where inserted_at between '{{ var("models")["backfill_start_date"] }}' and '{{ var("models")["backfill_end_date"] }}'
    {% else %}
        {% if is_incremental() %}
            where inserted_at > (select max(inserted_at) from {{ this }})
        {% endif %}
    {% endif %}
),

orders_source as (
    select 
        order_id
    from {{ source('dwh_raw', 'orders') }}
),

-- Validate order_id exists in fact_order (skip items if order missing)
validated_order_items as (
    select 
        oi.order_item_id,
        oi.order_id,
        oi.product_id,
        oi.quantity,
        oi.unit_price,
        oi.inserted_at
    from order_items_source oi
    inner join orders_source ord on oi.order_id = ord.order_id
),

-- Handle product lookup and set to '0' if missing
product_validated_items as (
    select 
        voi.order_item_id,
        voi.order_id,
        case 
            when dp.product_id is not null then voi.product_id
            else '0'  -- Auto corrected using semantic file: Set to '0' if product missing
        end as product_id,
        -- Ensure integer semantics for quantity, round if fractional
        round(voi.quantity, 0) as quantity,
        -- Handle null unit_price, treat as 0
        coalesce(voi.unit_price, 0) as unit_price,
        voi.inserted_at
    from validated_order_items voi
    left join {{ ref('dim_product') }} dp on voi.product_id = dp.product_id
),

-- Calculate extended amount, discount, and net amount
final_calculations as (
    select 
        pvi.order_item_id,
        pvi.order_id,
        pvi.product_id,
        pvi.quantity,
        pvi.unit_price,
        -- Calculate extended amount
        (pvi.quantity * pvi.unit_price) as extended_amount,
        -- Set discount to 0 as no discount context available
        0.00 as discount_amount,
        -- Calculate net amount, ensure non-negative (floor at 0)
        greatest(0, (pvi.quantity * pvi.unit_price) - 0.00) as net_amount,
        current_timestamp() as last_updated
    from product_validated_items pvi
)

select 
    order_item_id,
    order_id,
    product_id,
    quantity,
    unit_price,
    extended_amount,
    discount_amount,
    net_amount,
    last_updated
from final_calculations