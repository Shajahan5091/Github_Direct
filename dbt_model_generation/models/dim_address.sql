{{ config(materialized='table') }}

with source_addresses as (
    select 
        address_id,
        customer_id,
        address_line1,
        city,
        country,
        inserted_at
    from {{ source('dwh_raw', 'addresses') }}
),

customer_lookup as (
    select 
        customer_id
    from {{ ref('dim_customer') }}
),

validated_addresses as (
    select 
        src_addr.address_id,
        src_addr.customer_id,
        trim(src_addr.address_line1) as address_line1,
        upper(src_addr.city) as city,
        src_addr.country,
        src_addr.inserted_at as last_updated
    from source_addresses src_addr
    inner join customer_lookup cust_lookup
        on src_addr.customer_id = cust_lookup.customer_id
)

select 
    address_id,
    customer_id,
    address_line1,
    city,
    country,
    last_updated
from validated_addresses