WITH source_data AS (
    SELECT
        addresses.address_id,
        addresses.customer_id,
        addresses.address_line1,
        addresses.city,
        addresses.country,
        addresses.inserted_at
    FROM {{ source('dwh_raw', 'addresses') }} AS addresses
),

customer_lookup AS (
    SELECT
        dim_customer.customer_id
    FROM {{ ref('dim_customer') }} AS dim_customer
),

transformed AS (
    SELECT
        source_data.address_id,
        source_data.customer_id,
        TRIM(source_data.address_line1) AS address_line1,
        UPPER(source_data.city) AS city,
        source_data.country,
        source_data.inserted_at AS last_updated
    FROM source_data
    INNER JOIN customer_lookup
        ON source_data.customer_id = customer_lookup.customer_id
)

SELECT * FROM transformed