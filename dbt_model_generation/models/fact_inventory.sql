{{ config(
    materialized = 'table'
) }}

WITH source_data AS (
    SELECT
        inventory.product_id,
        inventory.supplier_id,
        inventory.stock_qty,
        inventory.warehouse_location,
        inventory.last_updated,
        inventory.inserted_at
    FROM {{ source('dwh_raw', 'inventory') }} AS inventory
    {% if var('backfill_flag') == 'true' %}
        WHERE inventory.inserted_at BETWEEN 'var("backfill_start_date")' AND 'var("backfill_end_date")'
    {% else %}
        {% if is_incremental() %}
            WHERE inventory.inserted_at > COALESCE(
                (SELECT MAX(last_updated) FROM BSL_MA.DWH_MA.fact_order_item), 
                TO_DATE('0001-01-01')
            )
        {% endif %}
    {% endif %}
),

product_lookup AS (
    SELECT
        source_data.product_id,
        source_data.supplier_id,
        source_data.stock_qty,
        source_data.warehouse_location,
        source_data.last_updated,
        source_data.inserted_at,
        COALESCE(dim_product.product_id, '0') AS validated_product_id -- auto corrected using semantic file
    FROM source_data
    LEFT JOIN {{ ref('dim_product') }} AS dim_product
        ON source_data.product_id = dim_product.product_id
),

supplier_lookup AS (
    SELECT
        product_lookup.product_id,
        product_lookup.supplier_id,
        product_lookup.stock_qty,
        product_lookup.warehouse_location,
        product_lookup.last_updated,
        product_lookup.inserted_at,
        product_lookup.validated_product_id,
        COALESCE(dim_supplier.supplier_id, '0') AS validated_supplier_id -- auto corrected using semantic file
    FROM product_lookup
    LEFT JOIN {{ ref('dim_supplier') }} AS dim_supplier
        ON product_lookup.supplier_id = dim_supplier.supplier_id
),

latest_snapshot_flag AS (
    SELECT
        supplier_lookup.product_id,
        supplier_lookup.supplier_id,
        supplier_lookup.stock_qty,
        supplier_lookup.warehouse_location,
        supplier_lookup.last_updated,
        supplier_lookup.inserted_at,
        supplier_lookup.validated_product_id,
        supplier_lookup.validated_supplier_id,
        ROW_NUMBER() OVER (
            PARTITION BY supplier_lookup.product_id, supplier_lookup.supplier_id 
            ORDER BY supplier_lookup.last_updated DESC
        ) = 1 AS is_latest
    FROM supplier_lookup
),

transformed AS (
    SELECT
        latest_snapshot_flag.validated_product_id AS product_id,
        latest_snapshot_flag.validated_supplier_id AS supplier_id,
        CASE 
            WHEN latest_snapshot_flag.stock_qty < 0 THEN NULL 
            ELSE latest_snapshot_flag.stock_qty 
        END AS stock_qty, -- auto corrected using semantic file
        UPPER(latest_snapshot_flag.warehouse_location) AS warehouse_location,
        DATE(latest_snapshot_flag.last_updated) AS snapshot_date,
        latest_snapshot_flag.is_latest,
        latest_snapshot_flag.inserted_at AS last_updated
    FROM latest_snapshot_flag
)

SELECT * FROM transformed