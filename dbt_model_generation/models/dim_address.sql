{{ config(materialized='table') }}

with source_addresses as (
    select 
        address_id,
        customer_id,
        address_line1,
        city,
        country,
        inserted_at
    from {{ source('dwh_raw', 'addresses') }}
),

dim_customer_lookup as (
    select 
        customer_id
    from {{ ref('dim_customer') }}
),

transformed_addresses as (
    select 
        addr.address_id,
        addr.customer_id,
        trim(addr.address_line1) as address_line1,
        upper(addr.city) as city,
        addr.country,
        addr.inserted_at as last_updated
    from source_addresses addr
    inner join dim_customer_lookup cust
        on addr.customer_id = cust.customer_id
)

select * from transformed_addresses