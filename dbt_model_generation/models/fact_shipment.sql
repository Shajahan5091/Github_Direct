MODEL NAME    : fact_shipment
TARGET TABLE  : BSL_MA.DWH_MA.FACT_SHIPMENT
SOURCE TABLES : BSL_RAW.DWH_RAW.SHIPMENTS, BSL_MA.DWH_MA.FACT_ORDER
DESCRIPTION   : Fact table for shipment data with order lookup and 
                calculated days to ship metrics
PREREQUISITES : FACT_ORDER table must exist and be populated
PARAMETER     : None
AUTHOUR       : AI Generated by Shajahan - 2025-11-18
------------------------------------------------------------------------
#}

{{ config(
    materialized='incremental',
    unique_key='shipment_id'
) }}

with shipments_source as (
    select 
        shipment_id,
        order_id,
        shipped_date,
        carrier,
        tracking_number
    from {{ source('dwh_raw', 'shipments') }}
    {% if is_incremental() %}
        where shipped_date >= (select max(shipped_date) from {{ this }})
    {% endif %}
),

orders_lookup as (
    select 
        order_id,
        order_date
    from {{ ref('fact_order') }}
),

shipment_with_order_data as (
    select 
        shipments.shipment_id,
        shipments.order_id,
        shipments.shipped_date::date as shipped_date,
        upper(shipments.carrier) as carrier,
        shipments.tracking_number,
        orders.order_date,
        greatest(0, datediff('day', orders.order_date::date, shipments.shipped_date::date)) as days_to_ship
    from shipments_source as shipments
    inner join orders_lookup as orders
        on shipments.order_id = orders.order_id
),

final_shipment_data as (
    select 
        shipment_id,
        order_id,
        shipped_date,
        carrier,
        tracking_number,
        days_to_ship,
        current_timestamp() as last_updated
    from shipment_with_order_data
)

select * from final_shipment_data