{{ config(
    materialized='table'
) }}

with source_products as (
    select
        product_id,
        product_name,
        price
    from {{ source('dwh_raw', 'products') }}
),

transformed_products as (
    select
        source_products.product_id,
        -- Title-case product name and trim excessive spaces
        initcap(trim(regexp_replace(source_products.product_name, '\\s+', ' '))) as product_name,
        -- Direct mapping for price
        source_products.price,
        -- Price band logic: 'Low' < 50, 'Mid' 50–199.99, 'High' 200–499.99, 'Premium' >= 500
        case
            when source_products.price < 50 then 'Low'
            when source_products.price >= 50 and source_products.price <= 199.99 then 'Mid'
            when source_products.price >= 200 and source_products.price <= 499.99 then 'High'
            when source_products.price >= 500 then 'Premium'
            else 'Unknown'
        end as price_band
    from source_products
)

select
    transformed_products.product_id,
    transformed_products.product_name,
    transformed_products.price,
    transformed_products.price_band
from transformed_products