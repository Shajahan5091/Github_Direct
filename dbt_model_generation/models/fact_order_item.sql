with source_order_items as (
    select 
        order_item_id,
        order_id,
        product_id,
        quantity,
        unit_price,
        inserted_at
    from {{ source('dwh_raw', 'order_items') }}
    {% if var('models')['backfill_flag'] == 'true' %}
        where inserted_at between '{{ var("models")["backfill_start_date"] }}' and '{{ var("models")["backfill_end_date"] }}'
    {% else %}
        {% if is_incremental() %}
            where inserted_at > (select max(inserted_at) from {{ this }})
        {% endif %}
    {% endif %}
),

source_orders as (
    select 
        order_id
    from {{ source('dwh_raw', 'orders') }}
),

validated_order_items as (
    select 
        order_items.order_item_id,
        order_items.order_id,
        -- Auto corrected using semantic file: product_id lookup with default fallback
        case 
            when order_items.product_id is null then '0'
            else order_items.product_id
        end as product_id,
        -- Auto corrected using semantic file: ensure integer semantics
        round(coalesce(order_items.quantity, 0)) as quantity,
        coalesce(order_items.unit_price, 0) as unit_price,
        order_items.inserted_at
    from source_order_items as order_items
    inner join source_orders as orders
        on order_items.order_id = orders.order_id
),

calculated_amounts as (
    select 
        order_item_id,
        order_id,
        product_id,
        quantity,
        unit_price,
        -- Calculate extended amount
        quantity * unit_price as extended_amount,
        -- Set discount amount to 0 as no discount context available
        0.00 as discount_amount,
        inserted_at
    from validated_order_items
),

final_calculations as (
    select 
        order_item_id,
        order_id,
        product_id,
        quantity,
        unit_price,
        extended_amount,
        discount_amount,
        -- Calculate net amount ensuring non-negative value
        greatest(extended_amount - discount_amount, 0) as net_amount,
        inserted_at,
        current_timestamp() as last_updated
    from calculated_amounts
)

select 
    order_item_id,
    order_id,
    product_id,
    quantity,
    unit_price,
    extended_amount,
    discount_amount,
    net_amount,
    last_updated
from final_calculations