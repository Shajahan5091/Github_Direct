TARGET TABLE  : BSL_MA.DWH_MA.FACT_ORDER
SOURCE TABLES : BSL_RAW.DWH_RAW.ORDERS
DESCRIPTION   : This model creates a fact table for orders by transforming
                raw order data with proper data type conversions, status
                normalization, and data quality validations.
PREREQUISITES : Source table BSL_RAW.DWH_RAW.ORDERS must exist
PARAMETER     : backfill_flag, backfill_start_date, backfill_end_date
