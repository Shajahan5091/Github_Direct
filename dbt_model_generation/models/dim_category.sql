    SELECT
        category_id,
        category_name,
        parent_category_id,
        is_active,
        created_at,
        inserted_at
    FROM {{ source('dwh_raw', 'categories') }}
    {% if var('backfill_flag') == 'true' %}
        WHERE inserted_at BETWEEN '{{ var("backfill_start_date") }}' AND '{{ var("backfill_end_date") }}'
    {% else %}
        {% if is_incremental() %}
            WHERE inserted_at > COALESCE( (SELECT MAX(last_updated) FROM BSL_MA.DWH_MA.dim_category), TO_DATE('0001-01-01'))
        {% endif %}
    {% endif %}
),

parent_categories AS (
    SELECT
        category_id,
        category_name AS parent_category_name
    FROM {{ source('dwh_raw', 'categories') }}
),

transformed AS (
    SELECT
        source_categories.category_id,
        TRIM(INITCAP(source_categories.category_name)) AS category_name, -- auto corrected using semantic file
        source_categories.parent_category_id,
        CASE 
            WHEN source_categories.parent_category_id IS NOT NULL 
            THEN CONCAT(parent_categories.parent_category_name, ' > ', TRIM(INITCAP(source_categories.category_name)))
            ELSE TRIM(INITCAP(source_categories.category_name))
        END AS category_path,
        COALESCE(source_categories.is_active, TRUE) AS is_active,
        source_categories.inserted_at AS last_updated -- auto corrected using semantic file
    FROM source_data AS source_categories
    LEFT JOIN parent_categories 
        ON source_categories.parent_category_id = parent_categories.category_id
)

