{{ config(
    materialized='incremental',
    unique_key='order_item_id',
    incremental_strategy='merge'
) }}

WITH source_order_items AS (
    SELECT
        order_item_id,
        order_id,
        product_id,
        quantity,
        unit_price,
        inserted_at
    FROM {{ source('dwh_raw', 'order_items') }}
    {% if var('backfill_flag') == 'true' %}
        WHERE inserted_at BETWEEN '{{ var("backfill_start_date") }}' AND '{{ var("backfill_end_date") }}'
    {% else %}
        {% if is_incremental() %}
            WHERE inserted_at > COALESCE(
                (SELECT MAX(last_updated) FROM BSL_MA.DWH_MA.fact_order_item), 
                TO_DATE('0001-01-01')
            )
        {% endif %}
    {% endif %}
),

source_orders AS (
    SELECT
        order_id,
        total_amount
    FROM {{ source('dwh_raw', 'orders') }}
),

order_items_with_order_validation AS (
    SELECT
        order_items.order_item_id,
        order_items.order_id,
        order_items.product_id,
        order_items.quantity,
        order_items.unit_price,
        order_items.inserted_at
    FROM source_order_items AS order_items
    INNER JOIN source_orders AS orders
        ON order_items.order_id = orders.order_id
    -- Skip item if order_id missing in fact_order as per notes
),

product_lookup_applied AS (
    SELECT
        order_items.order_item_id,
        order_items.order_id,
        -- If product missing set product_id to 0 as per notes -- auto corrected using semantic file
        COALESCE(order_items.product_id, '0') AS product_id,
        -- Ensure integer semantics; round if fractional as per notes
        ROUND(order_items.quantity) AS quantity,
        -- If unit_price null treat as 0 as per notes
        COALESCE(order_items.unit_price, 0) AS unit_price,
        order_items.inserted_at
    FROM order_items_with_order_validation AS order_items
),

calculated_amounts AS (
    SELECT
        order_items.order_item_id,
        order_items.order_id,
        order_items.product_id,
        order_items.quantity,
        order_items.unit_price,
        -- Compute quantity * unit_price
        (order_items.quantity * order_items.unit_price) AS extended_amount,
        -- Set 0 if no discount context as per notes
        0.00 AS discount_amount,
        order_items.inserted_at
    FROM product_lookup_applied AS order_items
),

final_calculations AS (
    SELECT
        order_item_id,
        order_id,
        product_id,
        quantity,
        unit_price,
        extended_amount,
        discount_amount,
        -- Compute extended_amount - discount_amount; ensure non-negative; floor at 0 as per notes
        GREATEST(extended_amount - discount_amount, 0) AS net_amount,
        inserted_at AS last_updated
    FROM calculated_amounts
)

SELECT * FROM final_calculations