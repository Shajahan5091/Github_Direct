WITH source_order_items AS (
    SELECT
        order_item_id,
        order_id,
        product_id,
        quantity,
        unit_price,
        inserted_at
    FROM {{ source('dwh_raw', 'order_items') }}
    {% if var('backfill_flag') == 'true' %}
        WHERE inserted_at BETWEEN '{{ var("backfill_start_date") }}' AND '{{ var("backfill_end_date") }}'
    {% else %}
        {% if is_incremental() %}
            WHERE inserted_at > COALESCE(
                (SELECT MAX(last_updated) FROM BSL_MA.DWH_MA.fact_order_item), 
                TO_DATE('0001-01-01')
            )
        {% endif %}
    {% endif %}
),

source_orders AS (
    SELECT
        order_id,
        customer_id,
        order_date,
        status,
        total_amount
    FROM {{ source('dwh_raw', 'orders') }}
),

source_products AS (
    SELECT
        product_id,
        product_name,
        category_id,
        price
    FROM {{ source('dwh_raw', 'products') }}
),

validated_order_items AS (
    SELECT
        order_items.order_item_id,
        order_items.order_id,
        CASE 
            WHEN products.product_id IS NULL THEN '0'  -- auto corrected using semantic file: If product missing set product_id to 0
            ELSE order_items.product_id 
        END AS product_id,
        ROUND(order_items.quantity) AS quantity,  -- auto corrected using semantic file: Ensure integer semantics; round if fractional
        COALESCE(order_items.unit_price, 0) AS unit_price,  -- auto corrected using semantic file: If unit_price null treat as 0
        order_items.inserted_at
    FROM source_order_items AS order_items
    INNER JOIN source_orders AS orders
        ON order_items.order_id = orders.order_id  -- auto corrected using semantic file: Skip item if order_id missing in fact_order
    LEFT JOIN source_products AS products
        ON order_items.product_id = products.product_id
),

calculated_metrics AS (
    SELECT
        order_item_id,
        order_id,
        product_id,
        quantity,
        unit_price,
        (quantity * unit_price) AS extended_amount,
        0.00 AS discount_amount,  -- auto corrected using semantic file: Set 0 if no discount context
        inserted_at
    FROM validated_order_items
),

final_calculations AS (
    SELECT
        order_item_id,
        order_id,
        product_id,
        quantity,
        unit_price,
        extended_amount,
        discount_amount,
        GREATEST(extended_amount - discount_amount, 0) AS net_amount,  -- auto corrected using semantic file: Ensure non-negative; floor at 0
        inserted_at AS last_updated
    FROM calculated_metrics
)

SELECT * FROM final_calculations