MODEL NAME    : fact_shipment
TARGET TABLE  : BSL_MA.DWH_MA.FACT_SHIPMENT
SOURCE TABLES : BSL_RAW.DWH_RAW.SHIPMENTS, BSL_RAW.DWH_RAW.ORDERS
DESCRIPTION   : This model creates a fact table for shipments with 
                calculated days to ship and transformed carrier data.
                Includes incremental loading capability.
PREREQUISITES : Source tables SHIPMENTS and ORDERS must exist
PARAMETER     : None
AUTHOUR       : AI Generated by Shajahan - 2025-11-19
------------------------------------------------------------------------
#}

{{ config(materialized='incremental') }}

with source_shipments as (
    select 
        shipment_id,
        order_id,
        shipped_date,
        carrier,
        tracking_number
    from {{ source('dwh_raw', 'shipments') }}
    {% if is_incremental() %}
        where shipped_date > (select max(shipped_date) from {{ this }})
    {% endif %}
),

source_orders as (
    select 
        order_id,
        order_date
    from {{ source('dwh_raw', 'orders') }}
),

shipment_with_order_data as (
    select 
        shipments.shipment_id,
        shipments.order_id,
        shipments.shipped_date::date as shipped_date,
        shipments.carrier,
        shipments.tracking_number,
        orders.order_date
    from source_shipments as shipments
    inner join source_orders as orders
        on shipments.order_id = orders.order_id
),

final_shipment_fact as (
    select 
        shipment_id,
        order_id,
        shipped_date,
        greatest(0, datediff('day', order_date::date, shipped_date)) as days_to_ship, -- auto corrected using semantic file
        upper(carrier) as carrier,
        tracking_number,
        current_timestamp() as last_updated
    from shipment_with_order_data
)

select * from final_shipment_fact