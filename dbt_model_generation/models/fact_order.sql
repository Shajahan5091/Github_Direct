{{ config(
    materialized='incremental',
    unique_key='order_id',
    incremental_strategy='merge'
) }}

WITH source_data AS (
    SELECT
        orders.order_id,
        orders.customer_id,
        orders.order_date,
        orders.status,
        orders.total_amount,
        orders.inserted_at
    FROM {{ source('dwh_raw', 'orders') }} AS orders
    {% if var('backfill_flag') == 'true' %}
        WHERE orders.inserted_at BETWEEN '{{ var("backfill_start_date") }}' AND '{{ var("backfill_end_date") }}'
    {% else %}
        {% if is_incremental() %}
            WHERE orders.inserted_at > COALESCE( (SELECT MAX(last_updated) FROM BSL_MA.DWH_MA.fact_order), TO_DATE('0001-01-01'))
        {% endif %}
    {% endif %}
),

customer_validation AS (
    SELECT
        source_data.order_id,
        source_data.customer_id,
        source_data.order_date,
        source_data.status,
        source_data.total_amount,
        source_data.inserted_at,
        dim_customer.customer_id AS valid_customer_id
    FROM source_data
    LEFT JOIN {{ ref('dim_customer') }} AS dim_customer
        ON source_data.customer_id = dim_customer.customer_id
),

transformed AS (
    SELECT
        customer_validation.order_id,
        customer_validation.customer_id,
        DATE(customer_validation.order_date) AS order_date, -- auto corrected using semantic file
        CASE 
            WHEN UPPER(customer_validation.status) IN ('PENDING', 'CANCELLED', 'SHIPPED', 'DELIVERED') 
            THEN UPPER(customer_validation.status)
            ELSE 'PENDING'
        END AS order_status,
        CASE 
            WHEN customer_validation.total_amount < 0 
            THEN NULL
            ELSE customer_validation.total_amount
        END AS total_amount,
        customer_validation.inserted_at AS last_updated
    FROM customer_validation
    WHERE customer_validation.valid_customer_id IS NOT NULL -- Skip orders with invalid customer_id
)

SELECT * FROM transformed