{{ config(
    materialized = 'table',
    alias = 'dim_product'
) }}

WITH source_data AS (
    SELECT
        products.product_id,
        products.product_name,
        products.price,
        products.inserted_at
    FROM {{ source('dwh_raw', 'products') }} AS products
    {% if var('models')['backfill_flag'] == 'true' %}
        WHERE products.inserted_at BETWEEN '{{ var("models")["backfill_start_date"] }}' AND '{{ var("models")["backfill_end_date"] }}'
    {% else %}
        {% if is_incremental() %}
            WHERE products.inserted_at > (SELECT MAX(inserted_at) FROM {{ this }})
        {% endif %}
    {% endif %}
),

transformed AS (
    SELECT
        source_data.product_id,
        INITCAP(TRIM(source_data.product_name)) AS product_name, -- Title-case product name and trim excessive spaces
        source_data.price,
        CASE 
            WHEN source_data.price < 50 THEN 'Low'
            WHEN source_data.price >= 50 AND source_data.price < 200 THEN 'Mid'
            WHEN source_data.price >= 200 AND source_data.price < 500 THEN 'High'
            WHEN source_data.price >= 500 THEN 'Premium'
        END AS price_band,
        source_data.inserted_at AS last_updated, -- auto corrected using semantic file
        CURRENT_TIMESTAMP() AS last_updated_timestamp
    FROM source_data
)

SELECT * FROM transformed