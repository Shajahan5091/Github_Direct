with shipments_source as (
    select 
        shipment_id,
        order_id,
        shipped_date,
        carrier,
        tracking_number,
        inserted_at  -- auto corrected using semantic file
    from {{ source('dwh_raw', 'shipments') }}
),

orders_source as (
    select 
        order_id,
        order_date
    from {{ source('dwh_raw', 'orders') }}
),

shipments_with_orders as (
    select 
        shipments.shipment_id,
        shipments.order_id,
        shipments.shipped_date::date as shipped_date,
        upper(shipments.carrier) as carrier,
        shipments.tracking_number,
        greatest(0, datediff('day', orders.order_date, shipments.shipped_date)) as days_to_ship,
        current_timestamp() as last_updated
    from shipments_source as shipments
    inner join orders_source as orders
        on shipments.order_id = orders.order_id
)

select 
    shipment_id,
    order_id,
    shipped_date,
    days_to_ship,
    carrier,
    tracking_number,
    last_updated
from shipments_with_orders

