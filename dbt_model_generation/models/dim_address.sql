SOURCE TABLES : BSL_RAW.DWH_RAW.ADDRESSES, BSL_RAW.DWH_RAW.CUSTOMERS
DESCRIPTION   : Dimension table for addresses with customer lookup and 
                data transformations including trimming whitespace and 
                uppercase city names
PREREQUISITES : dim_customer table must exist for customer lookup
