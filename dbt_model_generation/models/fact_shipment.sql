{{ config(
    materialized='incremental',
    unique_key='shipment_id',
    incremental_strategy='merge'
) }}

WITH source_shipments AS (
    SELECT
        shipment_id,
        order_id,
        shipped_date,
        carrier,
        tracking_number,
        inserted_at
    FROM {{ source('dwh_raw', 'shipments') }}
    {% if var('backfill_flag') == 'true' %}
        WHERE inserted_at BETWEEN '{{ var("backfill_start_date") }}' AND '{{ var("backfill_end_date") }}'
    {% else %}
        {% if is_incremental() %}
            WHERE inserted_at > COALESCE((SELECT MAX(last_updated) FROM BSL_MA.DWH_MA.FACT_SHIPMENT), TO_DATE('0001-01-01'))
        {% endif %}
    {% endif %}
),

source_orders AS (
    SELECT
        order_id,
        order_date
    FROM {{ source('dwh_raw', 'orders') }}
),

shipments_with_orders AS (
    SELECT
        shipments.shipment_id,
        shipments.order_id,
        shipments.shipped_date::DATE AS shipped_date, -- auto corrected using semantic file
        shipments.carrier,
        shipments.tracking_number,
        orders.order_date,
        shipments.inserted_at
    FROM source_shipments AS shipments
    LEFT JOIN source_orders AS orders
        ON shipments.order_id = orders.order_id
    WHERE orders.order_id IS NOT NULL -- Skip if order missing
),

transformed AS (
    SELECT
        shipment_id,
        order_id,
        shipped_date,
        GREATEST(0, DATEDIFF('day', order_date::DATE, shipped_date)) AS days_to_ship, -- floor at 0 for negative values
        UPPER(carrier) AS carrier,
        tracking_number,
        inserted_at AS last_updated
    FROM shipments_with_orders
)

SELECT * FROM transformed