{{ config(
    materialized='incremental',
    unique_key='payment_id'
) }}

with source_payments as (
    select 
        payment_id,
        order_id,
        payment_date,
        payment_method,
        amount
    from {{ source('dwh_raw', 'payments') }}
    {% if var('models')['backfill_flag'] == 'true' %}
        where payment_date between '{{ var("models")["backfill_start_date"] }}' and '{{ var("models")["backfill_end_date"] }}'
    {% endif %}
    {% if is_incremental() %}
        and payment_date > (select max(payment_date) from {{ this }})
    {% endif %}
),

source_orders as (
    select 
        order_id,
        total_amount
    from {{ source('dwh_raw', 'orders') }}
),

payment_aggregates as (
    select 
        payments.order_id,
        sum(payments.amount) as total_payments
    from source_payments payments
    group by payments.order_id
),

transformed_payments as (
    select 
        payments.payment_id,
        payments.order_id,
        payments.payment_date::date as payment_date,
        case 
            when upper(payments.payment_method) in ('CREDIT CARD', 'DEBIT CARD') then 'CARD'
            when upper(payments.payment_method) = 'PAYPAL' then 'UPI'
            when upper(payments.payment_method) in ('UPI', 'COD', 'NETBANKING') then upper(payments.payment_method)
            else 'CARD'
        end as payment_method,
        case 
            when payments.amount < 0 then null
            else payments.amount
        end as amount,
        case 
            when orders.total_amount is null then false
            when payment_agg.total_payments >= orders.total_amount then true
            else false
        end as is_fully_applied,
        current_timestamp() as last_updated
    from source_payments payments
    inner join source_orders orders 
        on payments.order_id = orders.order_id
    left join payment_aggregates payment_agg 
        on payments.order_id = payment_agg.order_id
)

select 
    payment_id,
    order_id,
    payment_date,
    payment_method,
    amount,
    is_fully_applied,
    last_updated
from transformed_payments