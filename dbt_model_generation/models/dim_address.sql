{{ config(materialized='table') }}

with source_addresses as (
    select
        address_id,
        customer_id,
        address_line1,
        city,
        country,
        inserted_at
    from {{ source('dwh_raw', 'addresses') }}
),

customer_lookup as (
    select
        customer_id
    from {{ ref('dim_customer') }}
),

validated_addresses as (
    select
        src_addr.address_id,
        src_addr.customer_id,
        src_addr.address_line1,
        src_addr.city,
        src_addr.country,
        src_addr.inserted_at
    from source_addresses as src_addr
    inner join customer_lookup as cust_lookup
        on src_addr.customer_id = cust_lookup.customer_id
),

final_transformation as (
    select
        address_id,
        customer_id,
        trim(address_line1) as address_line1,
        upper(city) as city,
        country,
        date(inserted_at) as inserted_at
    from validated_addresses
)

select * from final_transformation