{{ config(materialized='table') }}

with source_suppliers as (
    select
        suppliers.supplier_id,
        suppliers.supplier_name,
        suppliers.contact_email,
        suppliers.country,
        suppliers.active_flag,
        suppliers.inserted_at -- auto corrected using semantic file
    from {{ source('dwh_raw', 'suppliers') }} as suppliers
),

transformed_suppliers as (
    select
        source_suppliers.supplier_id,
        
        -- Trim and title-case supplier name
        initcap(trim(source_suppliers.supplier_name)) as supplier_name,
        
        -- Lowercase email and validate pattern, set null if invalid
        case 
            when regexp_like(lower(trim(source_suppliers.contact_email)), '^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,}$')
            then lower(trim(source_suppliers.contact_email))
            else null
        end as contact_email,
        
        -- Uppercase country
        upper(trim(source_suppliers.country)) as country,
        
        -- Direct mapping with null default to false
        coalesce(source_suppliers.active_flag, false) as is_active,
        
        -- Direct mapping for last_updated
        source_suppliers.inserted_at as last_updated
        
    from source_suppliers
)

select
    transformed_suppliers.supplier_id,
    transformed_suppliers.supplier_name,
    transformed_suppliers.contact_email,
    transformed_suppliers.country,
    transformed_suppliers.is_active,
    transformed_suppliers.last_updated
from transformed_suppliers