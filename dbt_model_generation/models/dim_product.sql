    materialized = 'table',
    alias = 'dim_product'
) }}

WITH source_data AS (
    SELECT
        product_id,
        product_name,
        price,
        inserted_at
    FROM {{ source('dwh_raw', 'products') }}
    {% if var('models')['backfill_flag'] == 'true' %}
        WHERE inserted_at BETWEEN '{{ var("models")["backfill_start_date"] }}' AND '{{ var("models")["backfill_end_date"] }}'
    {% else %}
        {% if is_incremental() %}
            WHERE inserted_at > (SELECT MAX(last_updated) FROM {{ this }})
        {% endif %}
    {% endif %}
),

transformed AS (
    SELECT
        product_id,
        INITCAP(TRIM(product_name)) AS product_name, -- Title-case product name, auto corrected using semantic file
        CAST(price AS NUMBER(10,2)) AS price,
        CASE 
            WHEN price < 50 THEN 'Low'
            WHEN price >= 50 AND price <= 199.99 THEN 'Mid'
            WHEN price >= 200 AND price <= 499.99 THEN 'High'
            WHEN price >= 500 THEN 'Premium'
        END AS price_band,
        inserted_at AS last_updated -- auto corrected using semantic file
    FROM source_data
)

SELECT * FROM transformed