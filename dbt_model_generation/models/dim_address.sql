{{ config(materialized='table') }}

with source_addresses as (
    select
        address_id,
        customer_id,
        address_line1,
        city,
        country,
        inserted_at
    from {{ source('dwh_raw', 'addresses') }}
),

validated_addresses as (
    select
        src_addr.address_id,
        src_addr.customer_id,
        trim(src_addr.address_line1) as address_line1,
        upper(src_addr.city) as city,
        src_addr.country,
        src_addr.inserted_at::date as inserted_at
    from source_addresses src_addr
    inner join {{ ref('dim_customer') }} dim_cust
        on src_addr.customer_id = dim_cust.customer_id
)

select
    address_id,
    customer_id,
    address_line1,
    city,
    country,
    inserted_at
from validated_addresses