MODEL NAME    : dim_product
TARGET TABLE  : bsl_ma.dwh_ma.dim_product
SOURCE TABLES : bsl_raw.dwh_raw.products
DESCRIPTION   : Product dimension table that transforms raw product data
                with proper formatting, price banding, and data quality
                checks for downstream analytics
PREREQUISITES : Source table bsl_raw.dwh_raw.products must be available
PARAMETER     : None
AUTHOR        : AI Generated by Shajahan - 2025-11-17
------------------------------------------------------------------------
#}

with source_products as (
    select
        product_id,
        product_name,
        price
    from {{ source('dwh_raw', 'products') }}
),

transformed_products as (
    select
        source_products.product_id,
        
        -- Apply title case and trim excessive spaces from product name
        initcap(trim(regexp_replace(source_products.product_name, '\\s+', ' '))) as product_name,
        
        -- Direct mapping of price with precision
        cast(source_products.price as number(10,2)) as price,
        
        -- Price band logic based on price ranges
        case
            when source_products.price < 50 then 'Low'
            when source_products.price >= 50 and source_products.price <= 199.99 then 'Mid'
            when source_products.price >= 200 and source_products.price <= 499.99 then 'High'
            when source_products.price >= 500 then 'Premium'
            else 'Unknown'
        end as price_band
        
    from source_products
)

select
    transformed_products.product_id,
    transformed_products.product_name,
    transformed_products.price,
    transformed_products.price_band
from transformed_products