{{ config(
    materialized='incremental',
    unique_key='order_id'
) }}

with source_orders as (
    select
        orders.order_id,
        orders.customer_id,
        orders.order_date,
        orders.status,
        orders.total_amount,
        orders.inserted_at -- auto corrected using semantic file
    from {{ source('dwh_raw', 'orders') }} as orders
    
    {% if var('models')['backfill_flag'] == 'true' %}
        where orders.inserted_at between '{{ var("models")["backfill_start_date"] }}' and '{{ var("models")["backfill_end_date"] }}'
    {% else %}
        {% if is_incremental() %}
            where orders.inserted_at > (select max(inserted_at) from {{ this }})
        {% endif %}
    {% endif %}
),

transformed_orders as (
    select
        source_orders.order_id,
        source_orders.customer_id,
        date(source_orders.order_date) as order_date,
        case 
            when upper(source_orders.status) in ('PENDING', 'CANCELLED', 'SHIPPED', 'DELIVERED') 
            then upper(source_orders.status)
            else 'PENDING'
        end as order_status,
        case 
            when source_orders.total_amount < 0 
            then null
            else source_orders.total_amount
        end as total_amount,
        source_orders.inserted_at as last_updated,
        current_timestamp() as last_updated_timestamp
    from source_orders
    where source_orders.customer_id is not null -- Skip orders with no matching customer
)

select
    transformed_orders.order_id,
    transformed_orders.customer_id,
    transformed_orders.order_date,
    transformed_orders.order_status,
    transformed_orders.total_amount,
    transformed_orders.last_updated,
    transformed_orders.last_updated_timestamp
from transformed_orders