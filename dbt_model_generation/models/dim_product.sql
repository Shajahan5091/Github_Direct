SOURCE TABLES : bsl_raw.dwh_raw.products
DESCRIPTION   : Dimension table for products with transformed product 
                information including price banding and data quality 
                validations
PREREQUISITES : Source table bsl_raw.dwh_raw.products must be available
PARAMETER     : None
