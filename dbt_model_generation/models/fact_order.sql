{{ config(
    materialized='incremental',
    unique_key='order_id'
) }}

with source_orders as (
    select * from {{ source('dwh_raw', 'orders') }}
),

transformed_orders as (
    select
        orders.order_id,
        orders.customer_id,
        orders.order_date::date as order_date,
        case 
            when upper(orders.status) in ('PENDING', 'CANCELLED', 'SHIPPED', 'DELIVERED') 
            then upper(orders.status)
            else 'PENDING'
        end as order_status,
        case 
            when orders.total_amount < 0 
            then null
            else orders.total_amount
        end as total_amount,
        current_timestamp() as last_updated
    from source_orders as orders
    where orders.customer_id is not null
    {% if var('models')['backfill_flag'] == 'true' %}
        and orders.order_date between '{{ var("models")["backfill_start_date"] }}' and '{{ var("models")["backfill_end_date"] }}'
    {% endif %}
    {% if is_incremental() %}
        and orders.order_date > (select max(order_date) from {{ this }})
    {% endif %}
)

select * from transformed_orders