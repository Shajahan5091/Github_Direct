WITH source_data AS (
    SELECT
        inventory.product_id,
        inventory.supplier_id,
        inventory.stock_qty,
        inventory.warehouse_location,
        inventory.last_updated,
        inventory.inserted_at
    FROM {{ source('dwh_raw', 'inventory') }} AS inventory
),

transformed AS (
    SELECT
        -- Product lookup with unknown handling
        CASE 
            WHEN source_data.product_id IS NULL THEN '0'
            ELSE source_data.product_id
        END AS product_id, -- auto corrected using semantic file
        
        -- Supplier lookup with unknown handling  
        CASE 
            WHEN source_data.supplier_id IS NULL THEN '0'
            ELSE source_data.supplier_id
        END AS supplier_id, -- auto corrected using semantic file
        
        -- Stock quantity with negative value handling
        CASE 
            WHEN source_data.stock_qty < 0 THEN NULL
            ELSE source_data.stock_qty
        END AS stock_qty,
        
        -- Uppercase warehouse location
        UPPER(source_data.warehouse_location) AS warehouse_location,
        
        -- Convert timestamp to date for snapshot_date
        DATE(source_data.last_updated) AS snapshot_date,
        
        -- Flag latest record per product_id + supplier_id combination
        CASE 
            WHEN source_data.last_updated = MAX(source_data.last_updated) OVER (
                PARTITION BY source_data.product_id, source_data.supplier_id
            ) THEN TRUE
            ELSE FALSE
        END AS is_latest,
        
        -- Direct mapping for last_updated
        source_data.inserted_at AS last_updated
        
    FROM source_data
)

