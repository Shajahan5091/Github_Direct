WITH source_data AS (
    SELECT
        categories.category_id,
        categories.category_name,
        categories.parent_category_id,
        categories.is_active,
        categories.inserted_at
    FROM {{ source('dwh_raw', 'categories') }} AS categories
),

parent_categories AS (
    SELECT
        parent_cat.category_id AS parent_category_id,
        parent_cat.category_name AS parent_category_name
    FROM {{ source('dwh_raw', 'categories') }} AS parent_cat
),

transformed AS (
    SELECT
        source_data.category_id,
        TRIM(INITCAP(source_data.category_name)) AS category_name,
        source_data.parent_category_id,
        CASE 
            WHEN source_data.parent_category_id IS NOT NULL 
            THEN CONCAT(
                TRIM(INITCAP(parent_categories.parent_category_name)), 
                ' > ', 
                TRIM(INITCAP(source_data.category_name))
            )
            ELSE TRIM(INITCAP(source_data.category_name))
        END AS category_path,
        COALESCE(source_data.is_active, TRUE) AS is_active, -- auto corrected using semantic file: If null default true
        source_data.inserted_at AS last_updated
    FROM source_data
    LEFT JOIN parent_categories
        ON source_data.parent_category_id = parent_categories.parent_category_id
)

