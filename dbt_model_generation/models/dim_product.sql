{{ config(materialized='table') }}

with source_products as (
    select 
        products.product_id,
        products.product_name,
        products.price,
        products.inserted_at
    from {{ source('dwh_raw', 'products') }} as products
    
    {% if var('models')['backfill_flag'] == 'true' %}
        where products.inserted_at between '{{ var("models")["backfill_start_date"] }}' and '{{ var("models")["backfill_end_date"] }}'
    {% else %}
        {% if is_incremental() %}
            where products.inserted_at > (select max(inserted_at) from {{ this }})
        {% endif %}
    {% endif %}
),

transformed_products as (
    select
        source_products.product_id,
        -- Title-case product name and trim excessive spaces
        initcap(trim(regexp_replace(source_products.product_name, '\\s+', ' '))) as product_name,
        -- Direct mapping with precision specification
        cast(source_products.price as number(10,2)) as price,
        -- Price banding logic
        case 
            when source_products.price < 50 then 'Low'
            when source_products.price >= 50 and source_products.price <= 199.99 then 'Mid'
            when source_products.price >= 200 and source_products.price <= 499.99 then 'High'
            when source_products.price >= 500 then 'Premium'
            else 'Unknown'
        end as price_band,
        -- Direct mapping for last_updated
        source_products.inserted_at as last_updated
    from source_products
)

select 
    transformed_products.product_id,
    transformed_products.product_name,
    transformed_products.price,
    transformed_products.price_band,
    transformed_products.last_updated
from transformed_products