with source_categories as (
    select * from {{ source('dwh_raw', 'categories') }}
),

parent_categories as (
    select 
        categories.category_id,
        categories.category_name,
        categories.parent_category_id,
        categories.is_active,
        parent_cat.category_name as parent_category_name
    from source_categories as categories
    left join source_categories as parent_cat
        on categories.parent_category_id = parent_cat.category_id
),

final as (
    select
        category_id,
        trim(initcap(category_name)) as category_name,
        parent_category_id,
        case 
            when parent_category_name is not null then 
                trim(initcap(parent_category_name)) || ' > ' || trim(initcap(category_name))
            else 
                trim(initcap(category_name))
        end as category_path,
        coalesce(is_active, true) as is_active -- auto corrected using semantic file: default to true if null
    from parent_categories
)

select * from final